-- SANKHYA.TRG_DLT_TGFFIN_EXCLUSAO
CREATE OR REPLACE TRIGGER SANKHYA.TRG_DLT_TGFFIN_EXCLUSAO
TRG_DLT_TGFFIN_EXCLUSAO AFTER DELETE ON TGFFIN
FOR EACH ROW

DECLARE
  P_SOLICITANTE           VARCHAR2(64);
  P_NTUSERNAME            VARCHAR2(30);
  P_PROGRAMNAME           VARCHAR2(80);
BEGIN
  IF STP_GET_ATUALIZANDO THEN
    RETURN;
  END IF;

-- a segunda parte ¿ para evitar pegar notas nao confirmadas que tem origem = 'E' e recdesp = 0
  IF :OLD.PROVISAO = 'N' AND (:OLD.ORIGEM = 'F' OR :OLD.NURENEG IS NOT NULL OR :OLD.RECDESP <> 0) THEN
    SELECT SUBSTR(OSUSER,1,30), SUBSTR(MACHINE,1,64), PROGRAM  
    INTO P_NTUSERNAME, P_SOLICITANTE, P_PROGRAMNAME
    FROM V$SESSION
    WHERE AUDSID = USERENV('SESSIONID')
      AND ROWNUM = 1;

    INSERT INTO TGFFIN_EXC (
      NUFIN,          CODEMP,        NUMNOTA,     SERIENOTA,
      DTNEG,          DESDOBRAMENTO, DHMOV,       DTVENCINIC,
      DTVENC,         DHBAIXA,       DTCONTAB,    DTCONTABBAIXA,
      CODPARC,        CODTIPOPER,    DHTIPOPER,   CODBCO,
      CODCTABCOINT,   CODNAT,        CODCENCUS,   CODPROJ,
      CODVEND,        CODMOEDA,      CODTIPTIT,   NUMDUPL,
      DESDOBDUPL,     NOSSONUM,      HISTORICO,   VLRDESDOB,
      VLRVENDOR,      VLRIRF,        VLRISS,      VLRCHEQUE,
      DESPCART,       ISSRETIDO,     VLRDESC,     VLRMULTA,
      VLRINSS,        TIPMULTA,      VLRJURO,     TIPJURO,
      BASEICMS,       ALIQICMS,      CODEMPBAIXA, CODTIPOPERBAIXA,
      DHTIPOPERBAIXA, VLRBAIXA,      NUMREMESSA,  AUTORIZADO,
      RECDESP,        PROVISAO,      ORIGEM,      TIPMARCCHEQ,
      NUNOTA,         NUBCO,         NUDEV,       NURENEG,
      RATEADO,        DTENTSAI,      CARTA,       CODUSUBAIXA,
      VLRPROV,        IRFRETIDO,     INSSRETIDO,  CARTAODESC,
      DHEXCLUSAO,     CODUSU,         NT_USERNAME,   HOSTNAME, PROGRAMA, NUCAIXA)
    VALUES (
     :OLD.NUFIN,          :OLD.CODEMP,        :OLD.NUMNOTA,     :OLD.SERIENOTA,
     :OLD.DTNEG,          :OLD.DESDOBRAMENTO, :OLD.DHMOV,       :OLD.DTVENCINIC,
     :OLD.DTVENC,         :OLD.DHBAIXA,       :OLD.DTCONTAB,    :OLD.DTCONTABBAIXA,
     :OLD.CODPARC,        :OLD.CODTIPOPER,    :OLD.DHTIPOPER,   :OLD.CODBCO,
     :OLD.CODCTABCOINT,   :OLD.CODNAT,        :OLD.CODCENCUS,   :OLD.CODPROJ,
     :OLD.CODVEND,        :OLD.CODMOEDA,      :OLD.CODTIPTIT,   :OLD.NUMDUPL,
     :OLD.DESDOBDUPL,     :OLD.NOSSONUM,      :OLD.HISTORICO,   :OLD.VLRDESDOB,
     :OLD.VLRVENDOR,      :OLD.VLRIRF,        :OLD.VLRISS,      :OLD.VLRCHEQUE,
     :OLD.DESPCART,       :OLD.ISSRETIDO,     :OLD.VLRDESC,     :OLD.VLRMULTA,
     :OLD.VLRINSS,        :OLD.TIPMULTA,      :OLD.VLRJURO,     :OLD.TIPJURO,
     :OLD.BASEICMS,       :OLD.ALIQICMS,      :OLD.CODEMPBAIXA, :OLD.CODTIPOPERBAIXA,
     :OLD.DHTIPOPERBAIXA, :OLD.VLRBAIXA,      :OLD.NUMREMESSA,  :OLD.AUTORIZADO,
     :OLD.RECDESP,        :OLD.PROVISAO,      :OLD.ORIGEM,      :OLD.TIPMARCCHEQ,
     :OLD.NUNOTA,         :OLD.NUBCO,         :OLD.NUDEV,       :OLD.NURENEG,
     :OLD.RATEADO,        :OLD.DTENTSAI,      :OLD.CARTA,       :OLD.CODUSUBAIXA,
     :OLD.VLRPROV,        :OLD.IRFRETIDO,     :OLD.INSSRETIDO,  :OLD.CARTAODESC,
     SYSDATE,     Tsiusu_Log_Pkg.V_CODUSULOG,         P_NTUSERNAME,    P_SOLICITANTE, P_PROGRAMNAME, :OLD.NUCAIXA);
  END IF;

END;

