-- SANKHYA.TRG_INC_UPD_DLT_TGFITE_RASTEST
CREATE OR REPLACE TRIGGER SANKHYA.TRG_INC_UPD_DLT_TGFITE_RASTEST
TRG_INC_UPD_DLT_TGFITE_RASTEST
AFTER INSERT OR UPDATE OR DELETE ON TGFITE FOR EACH ROW

DECLARE P_COUNT               NUMBER(5);
        P_TIPMOV              CHAR(1);
        P_CODTIPOPER          NUMBER(10);
        P_DHTIPOPER           DATE;
        P_DTENTSAI            DATE;
        P_STATUSNOTA          CHAR(1);
        P_STATUSNFE           CHAR(1);
        P_STATUSCTE           CHAR(1);
        P_STATUSNFSE          CHAR(1);
        P_VEMDACAB            NUMBER(10);
        P_ATUALLIVFIS         CHAR(1);
BEGIN  

  -- VERIFICANDO SE O CLIENTE UTILIZA ESTA FUNCIONALIDADE
  SELECT COUNT(*) INTO P_COUNT
  FROM TSIVARBD
  WHERE UTILIZA_RASTEST = 'N';
  IF P_COUNT > 0 THEN 
    RETURN;
  END IF;
  
  IF STP_GET_ATUALIZANDO THEN
    RETURN;
  END IF;
  
  /* QUANDO SUBSTITUINDO PRODUTO NÃO VALIDAR RASTREAMENTO */    
  IF (VARIAVEIS_PKG.V_SBPRODUTO) THEN
    RETURN;
  END IF;
  
  SELECT COUNT(1) INTO P_COUNT
  FROM TSIPAR
  WHERE CHAVE = 'SBPRODUTO';
  IF (P_COUNT <> 0) THEN
    RETURN;
  END IF; 
                        
  IF (INSERTING OR UPDATING) THEN
    BEGIN
       BEGIN
            SELECT CAB.TIPMOV, CAB.CODTIPOPER, CAB.DHTIPOPER, NVL(CAB.DTENTSAI, CAB.DTNEG), CAB.STATUSNOTA, TPO.ATUALLIVFIS
            INTO P_TIPMOV, P_CODTIPOPER, P_DHTIPOPER, P_DTENTSAI, P_STATUSNOTA, P_ATUALLIVFIS
            FROM TGFCAB CAB
            JOIN TGFTOP TPO ON (CAB.CODTIPOPER = TPO.CODTIPOPER AND CAB.DHTIPOPER = TPO.DHALTER)
            WHERE NUNOTA = :NEW.NUNOTA;
       EXCEPTION WHEN NO_DATA_FOUND THEN
            RAISE_APPLICATION_ERROR(-20101, 'Não foi encontrado cabeçalho para o item.');
       END;
              
       IF (:NEW.STATUSNOTA = 'L' AND NVL(:NEW.ATUALESTOQUE, 0) <> 0 AND :NEW.RESERVA = 'N') AND
       Get_Tem_Rastreamento_Itens(P_TIPMOV, :NEW.CODEMP, P_CODTIPOPER, P_DHTIPOPER, :NEW.CODPROD) = 'S' THEN     
            --VERIFICA SE É VENDA E BARRA ALTERAÇÃO DE CODPROD, CODLOCAL, CONTROLE E QTDNEG
            IF NVL(:OLD.ATUALESTOQUE, 0) <> 0 AND P_TIPMOV = 'V' AND  
               ((NVL(:OLD.CODPROD, 0) <> NVL(:NEW.CODPROD, 0)) OR
               (NVL(:OLD.CODLOCALORIG, 0) <> NVL(:NEW.CODLOCALORIG, 0)) OR
               (NVL(:OLD.CONTROLE, 0) <> NVL(:NEW.CONTROLE, 0)) OR
               (NVL(:OLD.QTDNEG, 0) <> NVL(:NEW.QTDNEG, 0))) THEN
               RAISE_APPLICATION_ERROR(-20101, 'Item com rastreamento não pode ser alterado em nota de venda confirmada.');
            END IF;        
            
            IF NVL(:NEW.ATUALESTOQUE, 0) <> 0 AND 
               ((:OLD.CODPROD <> :NEW.CODPROD) OR
                (:OLD.CODLOCALORIG <> :NEW.CODLOCALORIG) OR
                (:OLD.CONTROLE <> :NEW.CONTROLE) OR
                (:OLD.QTDNEG <> :NEW.QTDNEG) OR
                (NVL(:OLD.STATUSNOTA, 'A') <> :NEW.STATUSNOTA)) THEN
              IF :OLD.STATUSNOTA = 'L' AND Get_Tem_Rastreamento_Itens(P_TIPMOV, :OLD.CODEMP, P_CODTIPOPER, P_DHTIPOPER, :OLD.CODPROD) = 'S' THEN
                    Stp_Rastreamento_Est_Itens(:OLD.NUNOTA, P_TIPMOV, :OLD.CODEMP, P_DTENTSAI, P_CODTIPOPER, P_DHTIPOPER, :OLD.SEQUENCIA, :OLD.QTDNEG,
                                              :OLD.CODPROD, :OLD.CODLOCALORIG, :OLD.CONTROLE, :OLD.ATUALESTOQUE, 'D'); --D - DESFAZER OLD
              END IF;
              
              IF Get_Tem_Rastreamento_Itens(P_TIPMOV, :NEW.CODEMP, P_CODTIPOPER, P_DHTIPOPER, :NEW.CODPROD) = 'S' THEN
          
                Stp_Rastreamento_Est_Itens(:NEW.NUNOTA, P_TIPMOV, :NEW.CODEMP, P_DTENTSAI, P_CODTIPOPER, P_DHTIPOPER, :NEW.SEQUENCIA, :NEW.QTDNEG,
                                              :NEW.CODPROD, :NEW.CODLOCALORIG, :NEW.CONTROLE, :NEW.ATUALESTOQUE, 'F');--F - FAZER NEW         
              END IF;
              
            END IF;
       END IF;

       IF UPDATING AND 
          (:NEW.STATUSNOTA = 'L' AND :OLD.STATUSNOTA = 'L') AND 
          (:NEW.ATUALESTOQUE = 0 AND :OLD.ATUALESTOQUE <> 0) AND 
          :OLD.RESERVA = 'N' AND Get_Tem_Rastreamento_Itens(P_TIPMOV, :OLD.CODEMP, P_CODTIPOPER, P_DHTIPOPER, :OLD.CODPROD) = 'S' THEN
        Stp_Rastreamento_Est_Itens(:OLD.NUNOTA, P_TIPMOV, :OLD.CODEMP, P_DTENTSAI, P_CODTIPOPER, P_DHTIPOPER, :OLD.SEQUENCIA, :OLD.QTDNEG,
                                   :OLD.CODPROD, :OLD.CODLOCALORIG, :OLD.CONTROLE, :OLD.ATUALESTOQUE, 'D'); --D - DESFAZER OLD
       END IF;

    END;
  ELSIF (:OLD.STATUSNOTA = 'L' AND NVL(:OLD.ATUALESTOQUE, 0) <> 0 AND :OLD.RESERVA = 'N') THEN
    BEGIN
      BEGIN
        SELECT CAB.TIPMOV, CAB.CODTIPOPER, CAB.DHTIPOPER, CAB.STATUSNFE, CAB.STATUSCTE, CAB.STATUSNFSE
        INTO P_TIPMOV, P_CODTIPOPER, P_DHTIPOPER, P_STATUSNFE, P_STATUSCTE, P_STATUSNFSE
        FROM TGFCAB CAB
        WHERE NUNOTA = :OLD.NUNOTA;
      EXCEPTION WHEN NO_DATA_FOUND THEN
        P_TIPMOV := 'A';
      END;

      IF TGFCAB_PKG.V_CONTADOR <> 0 THEN
        P_VEMDACAB := TGFCAB_PKG.V_NUNOTA(TGFCAB_PKG.V_CONTADOR);
      ELSE
        P_VEMDACAB := 0;
      END IF;

      IF Get_Tem_Rastreamento_Itens(P_TIPMOV, :OLD.CODEMP, P_CODTIPOPER, P_DHTIPOPER, :OLD.CODPROD) = 'S' THEN
        SELECT CASE
                 WHEN NVL(:OLD.ATUALESTOQUE, 0) <> 0
                      AND P_TIPMOV = 'V'
                      AND P_VEMDACAB = 0
                      AND (P_STATUSNFE IN ('A','D','S') OR P_STATUSCTE IN ('A','D','S') OR P_STATUSNFSE = 'A')
                      AND NOT EXISTS (SELECT 1 FROM TGFCAN WHERE NUNOTA = :OLD.NUNOTA)
                      AND NOT EXISTS (SELECT 1 FROM TGFCAB_EXC WHERE NUNOTA = :OLD.NUNOTA)
                 THEN 1
                 ELSE 0
               END
          INTO P_COUNT
          FROM DUAL;
        IF P_COUNT = 1 THEN
          RAISE_APPLICATION_ERROR(-20101, 'Item com rastreamento não pode ser excluído em nota de venda confirmada, para desfazer a movimentação a nota deve ser excluída.');
        END IF;

        Stp_Rastreamento_Est_Itens(:OLD.NUNOTA, P_TIPMOV, :OLD.CODEMP, P_DTENTSAI, P_CODTIPOPER, P_DHTIPOPER, :OLD.SEQUENCIA, :OLD.QTDNEG,
                                   :OLD.CODPROD, :OLD.CODLOCALORIG, :OLD.CONTROLE, :OLD.ATUALESTOQUE, 'D'); --D - DESFAZER OLD
      END IF;
    END;
  END IF;
END;

