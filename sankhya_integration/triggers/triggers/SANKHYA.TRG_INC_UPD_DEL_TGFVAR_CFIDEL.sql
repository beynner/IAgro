-- SANKHYA.TRG_INC_UPD_DEL_TGFVAR_CFIDEL
CREATE OR REPLACE TRIGGER SANKHYA.TRG_INC_UPD_DEL_TGFVAR_CFIDEL
"SANKHYA".TRG_INC_UPD_DEL_TGFVAR_CFIDEL 
BEFORE UPDATE OR INSERT OR DELETE ON TGFVAR
FOR EACH ROW

DECLARE
 ERRMSG                   VARCHAR2(255);
 ERROR                    EXCEPTION;
 P_VALIDAR                BOOLEAN; 
 P_TIPMOV                 CHAR;
 P_CODPARC                NUMBER(10) := 0;
 P_VLRNOTA                FLOAT;
 P_VLRNOTAORIG            FLOAT;
 P_NUCFM                  NUMBER(10);
 P_VALOR                  FLOAT;
 P_DIFERENCA              FLOAT; 
 P_VNUNOTA                NUMBER(10);
 P_VNUNOTAORIG            NUMBER(10);
 P_COUNT                  NUMBER(5);
  CURSOR CUR_CFM IS
   SELECT NUCFM
   , VALOR 
   FROM TGFCFM
   WHERE NUNOTA = :NEW.NUNOTAORIG; 
BEGIN

  IF STP_GET_ATUALIZANDO THEN
    RETURN;
  END IF;
  
  IF DELETING THEN
    P_VNUNOTA     := :OLD.NUNOTA;
    P_VNUNOTAORIG := :OLD.NUNOTAORIG;  
  ELSE
    P_VNUNOTA     := :NEW.NUNOTA;
    P_VNUNOTAORIG := :NEW.NUNOTAORIG;  
  END IF;  
  
  BEGIN
    SELECT TIPMOV, VLRNOTA
    INTO P_TIPMOV, P_VLRNOTA 
    FROM TGFCAB 
    WHERE NUNOTA = P_VNUNOTA;
  EXCEPTION WHEN NO_DATA_FOUND THEN  
    RETURN;
  END;
  
  BEGIN
    SELECT CAB.CODPARC
    , CAB.VLRNOTA
    INTO P_CODPARC
    , P_VLRNOTAORIG   
    FROM TGFCAB CAB
    , TGFTOP TPO 
    WHERE CAB.NUNOTA    = P_VNUNOTAORIG 
    AND CAB.TIPMOV      = 'V'
    AND CAB.CODTIPOPER  = TPO.CODTIPOPER
    AND CAB.DHTIPOPER   = TPO.DHALTER
    AND TPO.CUPOMFISCAL = 'S';
  EXCEPTION WHEN NO_DATA_FOUND THEN  
    RETURN;
  END;
  
  P_VALIDAR := Fpodevalidar('TGFVAR');  
  
  IF (P_CODPARC > 0) AND (P_TIPMOV = 'D') THEN    
    IF P_VLRNOTA = P_VLRNOTAORIG THEN
      SELECT COUNT(1) INTO P_COUNT
      FROM TGFCFM WHERE NUNOTA = P_VNUNOTAORIG;
      
      IF P_COUNT > 0 THEN
        DELETE FROM TGFCFM WHERE NUNOTA = P_VNUNOTAORIG;
      END IF;
      
    ELSE
      P_DIFERENCA := P_VLRNOTAORIG - P_VLRNOTA;
      OPEN CUR_CFM;
      LOOP
        FETCH CUR_CFM INTO P_NUCFM
        , P_VALOR; 
        EXIT WHEN CUR_CFM%NOTFOUND;    
        IF P_DIFERENCA > 0 THEN        
          IF P_DIFERENCA >= P_VALOR THEN
            SELECT COUNT(1)
              INTO P_COUNT
            FROM TGFCFM WHERE NUCFM = P_NUCFM;
            
            IF P_COUNT > 0 THEN 
              DELETE FROM TGFCFM WHERE NUCFM = P_NUCFM;
            END IF;
            
          ELSE
            SELECT COUNT(1)
              INTO P_COUNT
            FROM TGFCFM 
            WHERE NUCFM = P_NUCFM;
            
            IF P_COUNT > 0 THEN  
              UPDATE TGFCFM SET VALOR = P_VALOR - P_DIFERENCA
              WHERE NUCFM = P_NUCFM;
            END IF;
          END IF;            
          P_DIFERENCA := P_DIFERENCA - P_VALOR;
        END IF;            
      END LOOP;
      CLOSE CUR_CFM;
    END IF;
  END IF;
  RETURN;  
EXCEPTION
  WHEN ERROR THEN
    IF (P_VALIDAR) THEN 
      RAISE_APPLICATION_ERROR(-20101, ERRMSG);
    END IF; 
END;

/
