-- SANKHYA.TRG_INC_UPD_TGFITE_TGAMOV
CREATE OR REPLACE TRIGGER SANKHYA.TRG_INC_UPD_TGFITE_TGAMOV
"SANKHYA".TRG_INC_UPD_TGFITE_TGAMOV AFTER INSERT OR UPDATE ON TGFITE 
FOR EACH ROW

DECLARE
  P_TIPMOV         TGFCAB.TIPMOV%TYPE;
  P_DTNEG          TGFCAB.DTNEG%TYPE;
  P_CODPARC        TGFCAB.CODPARC%TYPE;
  P_DTREFENT       TGFCAB.DTNEG%TYPE;
  P_DTREFCOB       TGFCAB.DTNEG%TYPE;
  P_NUMCONTRATO    TGFCAB.NUMCONTRATO%TYPE;
  P_COUNT          INT;
  P_TOPQUEBRA      INT;
  P_TOPIMPUREZA    INT;
  P_CODTOP         INT;
  P_NOTAMODELO     INT;
  ERROR                    EXCEPTION;
  ERRMSG            VARCHAR2(4000);
  P_VALIDAR BOOLEAN;
  P_IGNVALID INT;
BEGIN

  -- VERIFICANDO SE O CLIENTE UTILIZA ESTA FUNCIONALIDADE
  SELECT COUNT(*) INTO P_COUNT
  FROM TSIVARBD
  WHERE UTILIZA_TGAMOV = 'N';
  IF P_COUNT > 0 THEN 
    RETURN;
  END IF;

  IF STP_GET_ATUALIZANDO THEN
    RETURN;
  END IF;
  
  /* 
  sincronização de dados
  */
  P_VALIDAR := FPODEVALIDAR('TGFITE');
  
    IF NOT (P_VALIDAR) THEN
        RETURN;
    END IF;

  /* QDO SUBSTITUINDO PRODUTO, NÃO VALIDAR ESTOQUE */
  IF (VARIAVEIS_PKG.V_SBPRODUTO) THEN
    RETURN;
  END IF;
  
  SELECT COUNT(1) INTO P_COUNT
  FROM TSIPAR
  WHERE CHAVE = 'SBPRODUTO';
  IF (P_COUNT <> 0) THEN
    RETURN;
  END IF;   

    SELECT TIPMOV, DTNEG, CODPARC, NUMCONTRATO, CODTIPOPER
    INTO P_TIPMOV, P_DTNEG, P_CODPARC, P_NUMCONTRATO, P_CODTOP
    FROM TGFCAB
    WHERE NUNOTA = :NEW.NUNOTA;

    IF (P_TIPMOV NOT IN ('8','N','1','2','3','4')) THEN
      RETURN;
    END IF;

	SELECT COUNT(1) INTO P_IGNVALID
	FROM TSIPAR
	WHERE CHAVE = 'IGNNOTATOPIMP'
    AND LOGICO = 'S';
	
	IF P_IGNVALID = 0 THEN
		IF P_TIPMOV = 'N' THEN
		  BEGIN
			 SELECT INTEIRO INTO P_TOPIMPUREZA FROM TSIPAR WHERE CHAVE = 'TOPIMPUREZAE';
			 IF P_TOPIMPUREZA = 0 THEN
				 ERRMSG := 'Parâmetro TOPIMPUREZAE não está configurado.';
			   RAISE ERROR;
			 END IF;
			 IF P_TOPIMPUREZA = P_CODTOP THEN
			   RETURN;
			 END IF;
		  EXCEPTION
			 WHEN NO_DATA_FOUND THEN
			   ERRMSG := 'Parâmetro TOPIMPUREZAE não está configurado.';
		   RAISE ERROR;
		  END;
		ELSIF P_TIPMOV = '3' THEN
		  BEGIN
			 SELECT INTEIRO INTO P_TOPIMPUREZA FROM TSIPAR WHERE CHAVE = 'TOPIMPUREZAS';
			 IF P_TOPIMPUREZA = 0 THEN
			   ERRMSG := 'Parâmetro TOPIMPUREZAS não está configurado.';
               RAISE ERROR;
			 END IF;
			 IF P_TOPIMPUREZA = P_CODTOP THEN
			   RETURN;
			 END IF;
		  EXCEPTION
			 WHEN NO_DATA_FOUND THEN
			   ERRMSG := 'Parâmetro TOPIMPUREZAS não está configurado.';
		   RAISE ERROR; 
		  END;
       END IF;
	END IF;
    IF (P_TIPMOV IN ('N','1','3','4')) THEN
      Stp_Obtem_Datas_Armazem( P_TIPMOV, P_DTNEG, P_NUMCONTRATO, P_DTREFENT, P_DTREFCOB );
    END IF;

    IF UPDATING AND (:OLD.ATUALESTOQUE <> 0) AND (:OLD.RESERVA = 'N') THEN
      DELETE FROM TGAMOV
      WHERE NUNOTA    = :NEW.NUNOTA
        AND SEQUENCIA = :NEW.SEQUENCIA;
    END IF;

    IF P_TIPMOV IN ('3','4') THEN
		
		IF P_IGNVALID = 0 THEN
			  BEGIN
				SELECT INTEIRO INTO P_NOTAMODELO FROM TSIPAR WHERE CHAVE = 'NOTAMODQUEBRA';
			  EXCEPTION
				WHEN NO_DATA_FOUND THEN
				   ERRMSG := 'Parâmetro NOTAMODQUEBRA não está configurado.';
			   RAISE ERROR;
			  END;
			  IF P_NOTAMODELO = 0 THEN
			   ERRMSG := 'Parâmetro NOTAMODQUEBRA não está configurado.';
			 RAISE ERROR;
			  END IF;

			  BEGIN
				SELECT CODTIPOPER INTO P_TOPQUEBRA FROM TGFCAB WHERE NUNOTA = P_NOTAMODELO;
			  EXCEPTION
				WHEN NO_DATA_FOUND THEN
				ERRMSG := 'Não existe a nota modelo informada no parâmetro NOTAMODQUEBRA.';
			  RAISE ERROR;
			  END;
        END IF;
			IF P_CODTOP = P_TOPQUEBRA THEN  -- VERIFICANDO SE É UMA QUEBRA TÉCNICA
				P_TIPMOV := 'Q';
			ELSE
				P_TIPMOV := CASE WHEN :NEW.ATUALESTOQUE = 1 THEN 'E' ELSE 'S' END;
			END IF;
    ELSE
		P_TIPMOV := CASE WHEN :NEW.ATUALESTOQUE = 1 THEN 'E' ELSE 'S' END;
    END IF;

    IF P_DTREFENT IS NULL THEN
      ERRMSG := 'Erro calculando data de referencia para a nota.';
    RAISE ERROR;
    END IF;
    INSERT INTO TGAMOV ( NUNOTA, SEQUENCIA, DTNEG, CODPARC, CODPROD, CODLOCAL,
                         QTDNEG, ATUALESTOQUE, DTREFENT, DTREFCOB,TIPMOV, NUMCONTRATO, CONTROLE )
    VALUES (:NEW.NUNOTA, :NEW.SEQUENCIA, P_DTNEG, P_CODPARC, :NEW.CODPROD, :NEW.CODLOCALORIG,
            :NEW.QTDNEG, :NEW.ATUALESTOQUE, P_DTREFENT, P_DTREFCOB,P_TIPMOV, P_NUMCONTRATO,:NEW.CONTROLE );
  
  RETURN;
  
EXCEPTION
  WHEN OTHERS THEN
    /* 
    Sincronização de dados não faz validações
    */
     IF (P_VALIDAR) THEN
       IF SQLCODE <> 1 THEN
         ERRMSG := ERRMSG || '  ' || SQLERRM;
       END IF;
       RAISE_APPLICATION_ERROR(-20101, ERRMSG);       
     END IF;
END;

/
