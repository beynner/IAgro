-- SANKHYA.TRG_INC_TGFITE
CREATE OR REPLACE TRIGGER SANKHYA.TRG_INC_TGFITE
TRG_INC_TGFITE
BEFORE INSERT ON TGFITE FOR EACH ROW

DECLARE
    P_VALEST                 TGFGRU.VALEST%TYPE;
    ERROR                    EXCEPTION;
    P_CODEMPCAB              TGFCAB.CODEMP%TYPE;
    P_INICIOCONTEST          INT;
    P_COUNT                  INT := 0;
    P_CODGRUPOPROD           INT;
    P_TIPMOV                 CHAR(1);
    P_ATIVO                  CHAR(1);
    P_COMPLEMENTO            CHAR(1);
    P_ATUALIZOU              CHAR;
    P_AGRUPMIN               FLOAT;
    P_RESTO                  FLOAT;
    P_TEMIPIVENDA            CHAR;
    P_TEMIPICOMPRA           CHAR;
    P_CODEMPNEGOC            TGFITE.CODEMP%TYPE;
    P_CODTIPOPER             NUMBER(5);
    P_DHTIPOPER              DATE;
    P_TIPRES                 CHAR;
    P_QTDNEG                 FLOAT;
    P_ALES                   CHAR;
    P_RESERVADO_TOP          CHAR;
    P_ATUALESTOQUE_TOP       INT;
    P_ATUALESTOQUEMP_TOP     INT;
    P_VALIDAAGRUPMIN         CHAR;
    P_PENDENTE_TOP           CHAR;
    P_CFOSAIDA               TGFTOP.CODCFO_SAIDA%TYPE;
    P_CFOENTRADA             TGFTOP.CODCFO_ENTRADA%TYPE;
    P_CFO                    TGFTOP.CODCFO_ENTRADA%TYPE;
    P_CFOSAIDA_FORA          TGFTOP.CODCFO_SAIDA%TYPE;
    P_CFOENTRADA_FORA        TGFTOP.CODCFO_ENTRADA%TYPE;
    P_TIPCONTEST             TGFPRO.TIPCONTEST%TYPE;
    ERRMSG                   VARCHAR2(4000);
    P_VALIDAR                BOOLEAN;
    P_HABFLV                 CHAR(1);    
    P_TEMFLV                 CHAR(1);
    P_ADIARATUALEST          CHAR(1);
    P_CUPOMFISCAL            CHAR(1);  
    P_VALIDAVAREJO           INT;
    P_CSTIPI                 TGFITE.CSTIPI%TYPE;  
    P_CODENQIPI              TGFITE.CODENQIPI%TYPE; 
	P_PROGRAMNAME			 VARCHAR2(60);
	P_VALIDA_EST_RESERVAR	 TGFTOP.VALEST%TYPE;
    P_ADIARATUALIZACAOESTOQUE  CHAR(1);
	
BEGIN

  IF STP_GET_ATUALIZANDO THEN
    RETURN;
  END IF;
  
  P_CODENQIPI := :NEW.CODENQIPI;
  P_CSTIPI := :NEW.CSTIPI;
  
/* sincronizac?o de dados */
  P_VALIDAR := Fpodevalidar('TGFITE');

  P_ADIARATUALIZACAOESTOQUE := NVL(VARIAVEIS_PKG.V_ADIARATUALIZACAOESTOQUE, 'N');

  STP_VALIDA_ENQUADRAMENTO_IPI_2(P_CSTIPI, P_CODENQIPI);
    
  --Resolve problema de criacao de campos com default na TGFITE producao/w
  IF :NEW.GERAPRODUCAO IS NULL THEN
    :NEW.GERAPRODUCAO := 'S';
  END IF;
  
  -- OS 954116
  IF VARIAVEIS_PKG.V_DESMEMBRANDOBEM = 'N' THEN
    -- OS 902640
    SELECT COUNT(1) INTO P_COUNT
    FROM TGFLIV
    WHERE NUNOTA = :NEW.NUNOTA
      AND ORIGEM = 'E';

    IF P_COUNT > 0 THEN
        ERRMSG := 'Nota já foi gerada no Livro Fiscal.';
        RAISE ERROR;
    END IF;
    -- OS 902640
  END IF;
 
  SELECT COUNT(1) INTO P_COUNT
  FROM TGFVEN
  WHERE CODVEND = :NEW.CODVEND
  AND ATIVO = 'N';
  
  IF (P_COUNT <> 0) THEN
    Stp_Popula_Msg('TGFVEN');
  END IF;
  
  IF (LENGTH(TRIM(TRANSLATE(:NEW.SERIEINICIAL, ' +-.0123456789',' '))) IS NOT NULL) THEN
    ERRMSG := 'A coluna "SERIEINICIAL" deve conter somente numerais.';
    RAISE ERROR;
  END IF;
  
  IF (LENGTH(TRIM(TRANSLATE(:NEW.SERIEFINAL, ' +-.0123456789',' '))) IS NOT NULL) THEN
    ERRMSG := 'A coluna "SERIEFINAL" deve conter somente numerais.';
    RAISE ERROR;
  END IF;  
  
  -- CAMPOS DA CAB
  BEGIN
    SELECT C.TIPMOV, C.CODEMPNEGOC, C.CODTIPOPER, C.DHTIPOPER, C.CODEMP
    INTO  P_TIPMOV, P_CODEMPNEGOC, P_CODTIPOPER, P_DHTIPOPER, P_CODEMPCAB
    FROM  TGFCAB C
    WHERE NUNOTA = :NEW.NUNOTA;

  EXCEPTION
    WHEN NO_DATA_FOUND THEN
      ERRMSG := 'Nota não encontrada';
      RAISE ERROR;
  END;
  
  -- CAMPOS DA TOP
  SELECT CODCFO_ENTRADA
       , CODCFO_SAIDA
       , ATUALEST
       , ATUALESTMP
       , VALIDAAGRUPMIN
       , PENDENTE
       , CODCFO_ENTRADA_FORA
       , CODCFO_SAIDA_FORA
       , ADIARATUALEST
       , COMPLEMENTO
       , CUPOMFISCAL
	   , VALEST
    INTO P_CFOENTRADA
       , P_CFOSAIDA
       , P_ALES
       , P_ATUALESTOQUEMP_TOP
       , P_VALIDAAGRUPMIN
       , P_PENDENTE_TOP
       , P_CFOENTRADA_FORA
       , P_CFOSAIDA_FORA
       , P_ADIARATUALEST
       , P_COMPLEMENTO
       , P_CUPOMFISCAL
   	   , P_VALIDA_EST_RESERVAR
  FROM  TGFTOP
  WHERE CODTIPOPER = P_CODTIPOPER
    AND DHALTER = P_DHTIPOPER;

  IF ((:NEW.ATUALESTOQUE <> 0) AND (:NEW.RESERVA <> 'S') AND (:NEW.CONTROLE = ' ')) THEN
    SELECT CASE WHEN PEM.TIPCONTEST IS NOT NULL THEN PEM.TIPCONTEST ELSE P.TIPCONTEST END INTO P_TIPCONTEST
    FROM TGFPRO P LEFT JOIN TGFPEM PEM ON (P.CODPROD = PEM.CODPROD AND :NEW.CODEMP = PEM.CODEMP)
    WHERE P.CODPROD = :NEW.CODPROD; 

    IF (P_TIPCONTEST = 'L') THEN
      ERRMSG := 'Lote do produto '||TO_CHAR(:NEW.CODPROD)||' deve ser informado na nota de número único '||TO_CHAR(:NEW.NUNOTA)||'.';
      RAISE ERROR;
    END IF;
  END IF;    

  IF (P_VALIDAR) THEN
    IF (:NEW.PENDENTE = 'S' AND ABS(:NEW.QTDNEG) <= ABS(:NEW.QTDENTREGUE)) THEN
      :NEW.PENDENTE := 'N';
    ELSE
      IF (P_PENDENTE_TOP = 'N') THEN
        :NEW.PENDENTE := 'N';
      ELSIF (:NEW.PENDENTE = 'N') AND
        (ABS(:NEW.QTDNEG) > ABS(:NEW.QTDENTREGUE)) THEN
        :NEW.PENDENTE := 'S';
      END IF;
    END IF;
  END IF;
  
  BEGIN
    SELECT P.AGRUPMIN
    , CASE WHEN PEM.TEMIPIVENDA IS NULL OR PEM.TEMIPIVENDA = 'C' THEN P.TEMIPIVENDA ELSE PEM.TEMIPIVENDA END 
    , CASE WHEN PEM.TEMIPICOMPRA IS NULL OR PEM.TEMIPICOMPRA = 'C' THEN P.TEMIPICOMPRA ELSE PEM.TEMIPICOMPRA END 
    , P.CODGRUPOPROD, P.ATIVO, G.VALEST, G.TEMFLV
    INTO  P_AGRUPMIN, P_TEMIPIVENDA, P_TEMIPICOMPRA, P_CODGRUPOPROD, P_ATIVO, P_VALEST, P_TEMFLV
    FROM TGFPRO P LEFT JOIN TGFPEM PEM ON (P.CODPROD = PEM.CODPROD AND :NEW.CODEMP = PEM.CODEMP)
    , TGFGRU G
    WHERE P.CODPROD = :NEW.CODPROD
    AND P.CODGRUPOPROD = G.CODGRUPOPROD;
  EXCEPTION WHEN NO_DATA_FOUND THEN
    ERRMSG := 'Produto '||TO_CHAR(:NEW.CODPROD)||' não encontrado';
    RAISE ERROR;
  END;
    
  IF (:NEW.FATURAR <> 'F') THEN
       IF ((P_ATIVO = 'N') AND (:NEW.USOPROD <> 'D') AND (:NEW.USOPROD <> 'M')) THEN
      ERRMSG := 'Produto '||TO_CHAR(:NEW.CODPROD)||' não esta ativo.';
      RAISE ERROR;
    END IF;
    
    BEGIN
      SELECT ATIVO INTO P_ATIVO
      FROM TGFEMP
      WHERE CODEMP = :NEW.CODEMP;
    EXCEPTION
      WHEN NO_DATA_FOUND THEN
        ERRMSG := 'Empresa '||TO_CHAR(:NEW.CODEMP)||' não encontrada';
        RAISE ERROR;
    END;
    IF (P_ATIVO = 'N') THEN
      Stp_Popula_Msg('TGFEMP');
    END IF;
    IF (:NEW.CODLOCALORIG <> 0) THEN
      SELECT COUNT(1) INTO P_COUNT
      FROM TGFLOC
      WHERE CODLOCAL = :NEW.CODLOCALORIG
      AND ATIVO = 'S'
      AND ANALITICO = 'S';
      IF (P_COUNT = 0) THEN
        ERRMSG := 'Local não Ativo ou não Analítico ' || TO_CHAR(:NEW.CODLOCALORIG, '999999') || ' Prod' || TO_CHAR(:NEW.CODPROD, '999999999');
        RAISE ERROR;
      END IF;
    END IF;  
    
    IF ((P_VALIDAAGRUPMIN='S') AND (P_AGRUPMIN <> 1) AND (P_AGRUPMIN <> 0) AND (:NEW.QTDNEG <> 0) AND (P_COMPLEMENTO <> 'S') ) THEN
      P_RESTO := ROUND(MOD(:NEW.QTDNEG, P_AGRUPMIN), 9);
      IF (P_RESTO <> 0) THEN
        ERRMSG :=  'Quantidade negociada não e multipla do agrupamento minimo('||P_AGRUPMIN||') do produto '||:NEW.CODPROD||'.';
        RAISE ERROR;
      END IF;
    END IF;

    SELECT MIN(R.TIPREST) INTO P_TIPRES
      FROM TGFREP R, TGFPRO P
     WHERE ( (R.TIPREST = 'G' AND R.CODCOLREST = P.CODGRUPOPROD) OR
             (R.TIPREST = 'P' AND R.CODCOLREST = :NEW.CODPROD) )
       AND R.CODTIPOPER = P_CODTIPOPER
       AND P.CODPROD = :NEW.CODPROD
       AND R.RESTRICAO = 'N';
    IF (P_TIPRES IS NOT NULL) THEN
      IF (P_TIPRES = 'G') THEN
        ERRMSG := 'Este grupo de produto não pode ser usado com esta TOP.';
        RAISE ERROR;
      ELSIF (P_TIPRES = 'P') THEN
        ERRMSG := 'Este produto não pode ser usado com esta TOP.';
        RAISE ERROR;
      END IF;
    END IF;
    IF ( ( (P_TEMIPIVENDA = 'N') AND (P_TIPMOV IN ('P', 'V')) ) OR
      ( (P_TEMIPICOMPRA = 'N') AND (P_TIPMOV IN ('O', 'C', 'E')) ) ) AND
      ( (:NEW.BASEIPI <> 0) OR (:NEW.VLRIPI <> 0) ) THEN
      ERRMSG := 'A base e o valor do IPI devem ser zero (0)  para o PRODUTO: ' ||TO_CHAR(:NEW.CODPROD)|| ' NOTA: ' ||TO_CHAR(:NEW.NUNOTA)|| ' SEQUENCIA: ' ||TO_CHAR(:NEW.SEQUENCIA); 
      RAISE ERROR;
    END IF;
  END IF;
  IF (P_TIPMOV = 'T') THEN
    IF (:NEW.RESERVA = 'S') THEN
      ERRMSG := 'Transferencia não pode reservar.';
      RAISE ERROR;
    END IF;
    /*IF (:NEW.PENDENTE = 'S') THEN
      ERRMSG := 'Tranferencia n?o pode estar pendente.';
      RAISE ERROR;
    END IF;*/
    /*IF (:NEW.QTDENTREGUE <> 0) THEN
      ERRMSG := 'Transferencia n?o pode ter quantidade entregue diferente de zero.';
      RAISE ERROR;
    END IF;*/
    IF (:NEW.USOPROD = 'S') THEN
      ERRMSG := 'Transferencia não pode conter itens que são serviço.';
      RAISE ERROR;
    END IF;
  END IF;
  /* vai inserir com o atualestoque = 0 (zero), para quando faturar
  o pedido nao atualizar o estoque da nota porque a quantidade necessaria
  ainda estara reservada  */
  IF (:NEW.STATUSNOTA <> 'P') THEN
    IF (P_ALES = 'N') THEN
      P_RESERVADO_TOP      := 'N';
      P_ATUALESTOQUE_TOP := 0;
    ELSIF (P_ALES = 'E') THEN
      P_RESERVADO_TOP      := 'N';
      P_ATUALESTOQUE_TOP := 1 ;
    ELSIF (P_ALES = 'B') THEN
      P_RESERVADO_TOP     := 'N';
      P_ATUALESTOQUE_TOP  := -1;
    ELSIF (P_ALES = 'R') THEN
      P_RESERVADO_TOP      := 'S';
      P_ATUALESTOQUE_TOP := 1;
    END IF;

    IF (P_TIPMOV <> 'F') THEN
      IF (:NEW.SEQUENCIA < 0) THEN
        P_ATUALESTOQUEMP_TOP := P_ATUALESTOQUEMP_TOP * -1;
        P_ATUALESTOQUE_TOP := P_ATUALESTOQUE_TOP * -1;
      END IF;
      
      IF ((P_RESERVADO_TOP <> :NEW.RESERVA)) AND
          ((:NEW.USOPROD <> 'D') OR (:NEW.RESERVA <> 'N')) THEN
        SELECT LOGICO INTO P_HABFLV
        FROM TSIPAR
        WHERE CHAVE = 'HABFLV';
        
        --Quando P_HABFLV = 'S' and :NEW.USOPROD <> 'M' and P_TEMFLV = 'S' pode passar
        IF (P_HABFLV <> 'S' OR :NEW.USOPROD = 'M' OR P_TEMFLV <> 'S') THEN  
          ERRMSG := 'Reserva diferente da definição na TOP.';
          RAISE ERROR;
        END IF;
      END IF;
      
      IF (:NEW.USOPROD = 'M') AND (:NEW.RESERVA = 'N') THEN
        IF (P_ATUALESTOQUEMP_TOP <> :NEW.ATUALESTOQUE) AND NOT (P_ADIARATUALEST = 'S' AND :NEW.ATUALESTOQUE = 0 AND :NEW.STATUSNOTA <> 'L') THEN
          ERRMSG := 'Atualização de estoque de Mat.Prima diferente da definição na TOP.';
          RAISE ERROR;
        END IF;
      ELSIF (P_ATUALESTOQUE_TOP <> :NEW.ATUALESTOQUE) AND (:NEW.USOPROD <> 'D') THEN
        IF NOT (P_ADIARATUALEST = 'S' AND :NEW.ATUALESTOQUE = 0 AND :NEW.STATUSNOTA <> 'L') THEN
          ERRMSG := 'Atualização de estoque diferente da definição na TOP.';
          RAISE ERROR;
        END IF;
      ELSIF ((:NEW.USOPROD = 'D') AND
             (P_ATUALESTOQUE_TOP <> :NEW.ATUALESTOQUE) AND
             (P_ATUALESTOQUEMP_TOP <> :NEW.ATUALESTOQUE) AND (:NEW.ATUALESTOQUE <> 0)) THEN
        ERRMSG := 'Atualização de estoque diferente da definição na TOP.';
        RAISE ERROR;
      END IF;    
    END IF;
  END IF;

  IF (P_TIPMOV <> 'Z') THEN
    BEGIN
      SELECT INTEIRO INTO P_INICIOCONTEST FROM TSIPAR WHERE CHAVE = 'INICIOCONTEST';
    EXCEPTION
      WHEN NO_DATA_FOUND THEN
        P_INICIOCONTEST := 0;
    END;

    IF (:NEW.ATUALESTOQUE <> 0) AND (:NEW.NUNOTA >= P_INICIOCONTEST) AND ((:NEW.RESERVA='N') OR (:NEW.PENDENTE = 'S')) THEN
      IF  (:NEW.RESERVA = 'S') THEN
        P_QTDNEG := :NEW.QTDNEG - :NEW.QTDENTREGUE;
      ELSE
        P_QTDNEG := :NEW.QTDNEG;
      END IF;
      Estoque_Pkg.V_TRANSF := 'N';

  
    SELECT UPPER(PROGRAM)
      INTO P_PROGRAMNAME
      FROM V$SESSION
     WHERE AUDSID = USERENV('SESSIONID')
       AND ROWNUM = 1;
  
      -- MANOEL OS 823336
	  
	  IF P_VALEST <> 'N' AND P_PROGRAMNAME = 'FSTECF.EXE' THEN
        SELECT INTEIRO
          INTO P_VALIDAVAREJO 
          FROM TSIPAR WHERE CHAVE = 'VALIDAVAREJO';
          
		IF MOD(P_VALIDAVAREJO, 2) = 0 THEN
          Estoque_Pkg.V_TRANSF := 'S';
        END IF; 
      END IF;
	  
      IF P_TIPMOV = 'T' AND P_VALEST IN ('G','S') THEN
        IF P_CODEMPCAB = P_CODEMPNEGOC OR P_VALEST = 'G' THEN       
          Estoque_Pkg.V_TRANSF := 'S';
        ELSIF P_VALEST = 'S' THEN
          SELECT COUNT( DISTINCT NVL(EMPSOMA, CODEMP) )
          INTO P_COUNT
          FROM TGFEMP
          WHERE CODEMP IN (P_CODEMPCAB, P_CODEMPNEGOC);
          IF P_COUNT = 1 THEN
            Estoque_Pkg.V_TRANSF := 'S';
          END IF;
        END IF;
      END IF;

		VARIAVEIS_PKG.V_VALIDA_EST_RESERVAR := P_VALIDA_EST_RESERVAR = 'S' AND Get_Tsipar_Logico('REVALIDAESTRES') = 'S';

      IF P_ADIARATUALIZACAOESTOQUE <> 'S' THEN
	      Stp_Atualiza_Tgfest( :NEW.RESERVA, :NEW.ATUALESTOQUE, :NEW.CODEMP, :NEW.CODPROD, :NEW.CONTROLE, :NEW.CODLOCALORIG, P_QTDNEG, :NEW.STATUSLOTE, P_ATUALIZOU);
      END IF;

      Estoque_Pkg.V_TRANSF := 'N';

      IF (P_ATUALIZOU = 'N') THEN
        ERRMSG := '(000) Falha na atualização do estoque do produto:  ' ||TO_CHAR(:NEW.CODPROD, '999999999');
        RAISE ERROR;
      END IF;
    END IF;
  END IF;

IF (:NEW.FATURAR <> 'F') AND (:NEW.SEQUENCIA > 0) AND (P_VALIDAR) THEN
    P_CFO := :NEW.CODCFO;
    IF (P_TIPMOV = 'T') THEN
        IF :NEW.CODCFO = P_CFOENTRADA THEN
          P_CFO := P_CFOSAIDA;
        ELSIF :NEW.CODCFO = P_CFOSAIDA THEN
          P_CFO := P_CFOENTRADA;
        ELSIF :NEW.CODCFO = P_CFOENTRADA_FORA THEN
          P_CFO := P_CFOSAIDA_FORA;
        ELSIF :NEW.CODCFO = P_CFOSAIDA_FORA THEN
          P_CFO := P_CFOENTRADA_FORA;
        ELSIF :NEW.CODCFO BETWEEN 1000 AND 3000 THEN
          P_CFO := :NEW.CODCFO + 4000;
        ELSIF :NEW.CODCFO BETWEEN 5000 AND 7000 THEN
          BEGIN
            P_CFO := :NEW.CODCFO - 4000;
            IF P_CFO = 2404 THEN
              P_CFO := 2403;
            END IF;  
	        IF SUBSTR(P_CFO,2,4) BETWEEN 654 AND 656 THEN
	           P_CFO := P_CFO - 3;
	        END IF;
          END;
        ELSE
          P_CFO := :NEW.CODCFO;
        END IF;
    END IF;
    INSERT INTO TGFITE_INC (NUNOTA, SEQUENCIA, CFO, CODEMPNEGOC, TIPMOV, RESERVA, ATUALESTOQUE,
    CODEMP, CODPROD, CONTROLE, CODLOCAL, QTDNEG, QTDENTREGUE, USOPROD, ATUALESTTERC, TERCEIROS,
    PERCPUREZA, PERCGERMIN, STATUSLOTE, PENDENTE)
    VALUES (:NEW.NUNOTA, :NEW.SEQUENCIA, P_CFO, P_CODEMPNEGOC, P_TIPMOV, :NEW.RESERVA,
    :NEW.ATUALESTOQUE, :NEW.CODEMP, :NEW.CODPROD, :NEW.CONTROLE, :NEW.CODLOCALORIG, :NEW.QTDNEG,
    :NEW.QTDENTREGUE, :NEW.USOPROD, :NEW.ATUALESTTERC, :NEW.TERCEIROS,
    :NEW.PERCPUREZA, :NEW.PERCGERMIN, :NEW.STATUSLOTE, :NEW.PENDENTE);
  END IF;
 
  -- OS 717072
  -- OS 748512 SOLICITOU MANTER COM ARGUMENTO 9(10 estoura o tamanho do tipo de dados)
  :NEW.QTDNEG := ROUND(:NEW.QTDNEG,9);
  
  --756293  
  :NEW.QTDENTREGUE := ROUND(:NEW.QTDENTREGUE,9);  

  RETURN;
   
EXCEPTION
  WHEN OTHERS THEN
  /* 
  Sincronizac?o de dados n?o faz validac?es
  */
    Estoque_Pkg.V_TRANSF := 'N';
	VARIAVEIS_PKG.V_VALIDA_EST_RESERVAR := false;
    IF (P_VALIDAR) THEN 
       IF SQLCODE <> 1 THEN
         ERRMSG := ERRMSG || '  ' || SQLERRM;
       END IF;
       RAISE_APPLICATION_ERROR(-20101, ERRMSG);       
     END IF;
END;

