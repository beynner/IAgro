-- SANKHYA.TRG_DLT_TGFCAB_METAS
CREATE OR REPLACE TRIGGER SANKHYA.TRG_DLT_TGFCAB_METAS
"SANKHYA".TRG_DLT_TGFCAB_METAS BEFORE DELETE ON TGFCAB FOR EACH ROW

DECLARE
  P_COUNT           INT;
  ERRMSG            VARCHAR2(255);
  ERROR             EXCEPTION;
  P_VALIDAR         BOOLEAN;
  P_CODUSULOGADO    NUMBER(10);
BEGIN

  -- VERIFICANDO SE O CLIENTE UTILIZA ESTA FUNCIONALIDADE
  SELECT COUNT(1) INTO P_COUNT
  FROM TGMTRA 
  WHERE NUNOTA = :OLD.NUNOTA;
  IF P_COUNT = 0 THEN
    RETURN;
  END IF;

  IF STP_GET_ATUALIZANDO THEN
    RETURN;
  END IF;

  /* 
  Sincronização de dados
  */
  P_VALIDAR := FPODEVALIDAR('TGFCAB');
  P_CODUSULOGADO := STP_GET_CODUSULOGADO;
  SELECT COUNT(1) INTO P_COUNT
    FROM TGMTRA 
   WHERE NUNOTA = :OLD.NUNOTA
     AND STATUS = 'A'
     AND TIPO <> 'C'
     AND P_CODUSULOGADO <> 0
     AND CODUSULIB <> P_CODUSULOGADO;
  
  IF P_COUNT <> 0 THEN
    SELECT COUNT(1) INTO P_COUNT
    FROM TGFVAR VAR
    , TGMTRA TRA
    WHERE VAR.NUNOTA = :OLD.NUNOTA
    AND VAR.NUNOTA <> VAR.NUNOTAORIG
    AND VAR.NUNOTAORIG = TRA.NUNOTA;

    IF P_COUNT = 0 THEN    
      ERRMSG := 'Já existe aprovação de orçamento, não pode ser excluído. Nota de Nro Único: '|| TO_CHAR(:NEW.NUNOTA) ||'.';
    RAISE ERROR;
    END IF;
  END IF;
  
  DELETE FROM TGMTRA WHERE NUNOTA = :OLD.NUNOTA;
  RETURN;  
EXCEPTION
  WHEN ERROR THEN
    /* 
    Sincronização de dados não faz validações
    */
    IF (P_VALIDAR) THEN 
      RAISE_APPLICATION_ERROR(-20101, ERRMSG);
    END IF; 
   
  
END;

/
