-- SANKHYA.TRG_DLT_TGFITE_AFTER
CREATE OR REPLACE TRIGGER SANKHYA.TRG_DLT_TGFITE_AFTER
"SANKHYA".TRG_DLT_TGFITE_AFTER
AFTER DELETE ON TGFITE

DECLARE
  P_COUNT             INT;
  P_COUNT1            INT;
  P_ATUALEST          INT;
  P_NUNOTA            TGFITE.NUNOTA%TYPE;
  P_SEQUENCIA         TGFITE.SEQUENCIA%TYPE;
  P_PENDENTE          TGFITE.PENDENTE%TYPE;
  P_TIPMOV            TGFCAB.TIPMOV%TYPE;
  P_CODEMP            TGFITE.CODEMP%TYPE;
  P_CODPROD           TGFITE.CODPROD%TYPE;
  P_CONTROLE          TGFITE.CONTROLE%TYPE;
  P_CODLOCAL          TGFITE.CODLOCALORIG%TYPE;
  ERRMSG              VARCHAR2(255);
  ERROR               EXCEPTION;
  P_VALIDAR           BOOLEAN;
  P_SERIE             TGFSER.SERIE%TYPE;  
  P_PERCPUREZA             TGFITE.PERCPUREZA%TYPE;
  P_PERCGERMIN             TGFITE.PERCGERMIN%TYPE;

  CURSOR ITENS IS
    SELECT NUNOTA, SEQUENCIA, PENDENTE, TIPMOV, CODEMP, CODPROD, CONTROLE, CODLOCAL, PERCPUREZA, PERCGERMIN
    FROM TGFITE_DLT
    ORDER BY ATUALESTOQUE DESC;

  CURSOR NOTAS IS SELECT DISTINCT NUNOTA FROM TGFITE_DLT;

BEGIN


  IF STP_GET_ATUALIZANDO THEN
    RETURN;
  END IF;

  /* 
  sincronizac?o de dados
  */
  P_VALIDAR := Fpodevalidar('TGFITE');

/* atualizac?o do campo pendente da Cab */
  OPEN ITENS;
  OPEN NOTAS;
  DELETE FROM TGFITE_DLT;
  LOOP
    FETCH ITENS INTO P_NUNOTA, P_SEQUENCIA, P_PENDENTE, P_TIPMOV, P_CODEMP, P_CODPROD, P_CONTROLE, P_CODLOCAL, P_PERCPUREZA, P_PERCGERMIN;
    EXIT WHEN ITENS%NOTFOUND;
    IF (P_PENDENTE = 'S') THEN
      SELECT COUNT(1) INTO P_COUNT
      FROM TGFITE
      WHERE PENDENTE = 'S'
        AND NUNOTA = P_NUNOTA;

      SELECT COUNT(1) INTO P_COUNT1
      FROM TGFITE
      WHERE NUNOTA = P_NUNOTA;
      IF (P_COUNT = 0 ) AND (P_COUNT1 <> 0) THEN
        UPDATE TGFCAB
        SET PENDENTE = 'N'
        WHERE NUNOTA = P_NUNOTA
          AND PENDENTE = 'S';
      END IF;
    END IF;

    IF (P_TIPMOV = 'T') OR (P_TIPMOV = ' ') THEN
      DELETE FROM TGFITE WHERE NUNOTA = P_NUNOTA AND SEQUENCIA = (P_SEQUENCIA * (-1));
    END IF;

    SELECT COUNT(1), MIN(T2.SERIE), SUM(CASE WHEN SEQUENCIA > 0 THEN T2.ATUALESTOQUE ELSE 0 END)
    INTO P_COUNT, P_SERIE, P_ATUALEST
    FROM TGFSER T2
    WHERE T2.NUNOTA = P_NUNOTA
    AND T2.SEQUENCIA IN (P_SEQUENCIA, P_SEQUENCIA * (-1));

    IF P_COUNT <> 0 THEN
      
      DELETE FROM TGFSER T2 WHERE  T2.NUNOTA = P_NUNOTA AND T2.SEQUENCIA = P_SEQUENCIA;
      DELETE FROM TGFSSE T3 WHERE  T3.NUNOTA = P_NUNOTA AND T3.SEQNOTA = P_SEQUENCIA;
       
      IF P_VALIDAR AND P_ATUALEST <> 0 THEN
         
        SELECT SUM(ITE.ATUALESTOQUE) 
        INTO P_COUNT
        FROM TGFSER SER
        , TGFITE ITE
        WHERE ITE.NUNOTA = SER.NUNOTA
        AND ITE.SEQUENCIA = SER.SEQUENCIA
        AND SER.ATUALESTOQUE <> 0 --TRECHO INCLUIDO PARA QUE VALIDE DE ACORDO C/ A VGFSER 
        AND ITE.CODPROD = P_CODPROD
        AND ITE.CODEMP = P_CODEMP
        AND ITE.CODLOCALORIG = P_CODLOCAL
        AND SER.SERIE = P_SERIE;

        IF (P_COUNT < 0 OR P_COUNT > 1) THEN
          ERRMSG := 'Não pode ser excluido, o SALDO das SERIES ficara INCONSISTENTE no estoque.';
          RAISE ERROR;
        END IF;
      END IF;

    END IF;
    
    SELECT COUNT(1) INTO P_COUNT
    FROM TGFVAR T2
    WHERE T2.NUNOTA = P_NUNOTA 
      AND T2.SEQUENCIA = P_SEQUENCIA;
    IF P_COUNT <> 0 THEN
        DELETE  FROM TGFVAR T2  
        WHERE T2.NUNOTA = P_NUNOTA 
          AND T2.SEQUENCIA = P_SEQUENCIA;
    END IF;
    
    
    --Comentado OS 597694
    --Comentario retirado a pedido do Manoel pois esta linha serve para eliminar as linhas da TGFVAR provenientes de movimentac?o
    --com KIT e PRODUC?O
    SELECT COUNT(1) INTO P_COUNT
    FROM TGFVAR T2
    WHERE T2.NUNOTAORIG = P_NUNOTA 
      AND T2.SEQUENCIAORIG = P_SEQUENCIA;
    IF P_COUNT <> 0 THEN
        DELETE  FROM  TGFVAR T2  
        WHERE T2.NUNOTAORIG = P_NUNOTA 
          AND T2.SEQUENCIAORIG = P_SEQUENCIA;
    END IF;
    
    
    SELECT COUNT(1) INTO P_COUNT
    FROM TGFIDA IDA
    WHERE IDA.NUNOTA = P_NUNOTA 
      AND IDA.SEQUENCIA = P_SEQUENCIA;
    IF P_COUNT <> 0 THEN    
        DELETE FROM TGFIDA IDA
        WHERE IDA.NUNOTA = P_NUNOTA 
          AND IDA.SEQUENCIA = P_SEQUENCIA;
    END IF;
    
    SELECT COUNT(1) INTO P_COUNT
    FROM TGFEAC T2
    WHERE ((T2.NUNOTA   = P_NUNOTA) OR (NUPROD = P_NUNOTA)) 
      AND (T2.CODPROD  = P_CODPROD) 
      AND (T2.CONTROLE = P_CONTROLE)
      AND (T2.CODLOCAL = P_CODLOCAL);
    IF P_COUNT <> 0 THEN
        DELETE  FROM TGFEAC T2
        WHERE ((T2.NUNOTA   = P_NUNOTA) OR (NUPROD = P_NUNOTA))
          AND (T2.CODPROD  = P_CODPROD)
          AND (T2.CONTROLE = P_CONTROLE)
          AND (T2.CODLOCAL = P_CODLOCAL);
    END IF;
    
    SELECT COUNT(1) INTO P_COUNT
    FROM TGFIMN 
    WHERE NUNOTA = P_NUNOTA 
      AND SEQUENCIA = P_SEQUENCIA;
    IF P_COUNT <> 0 THEN
        DELETE FROM TGFIMN 
        WHERE NUNOTA = P_NUNOTA 
        AND SEQUENCIA = P_SEQUENCIA;
    END IF;
        
  END LOOP;
  CLOSE ITENS;

  /* atualizacao da provisao */
   LOOP
     FETCH NOTAS INTO P_NUNOTA;
     EXIT WHEN NOTAS%NOTFOUND;
      Provisao_Fin( P_NUNOTA );
   END LOOP;
   CLOSE NOTAS;
/*acabou a at. da provisao */

  RETURN;
   
EXCEPTION
  WHEN ERROR THEN
    /* 
    Sincronizac?o de dados n?o faz validac?es
    */
    IF (P_VALIDAR) THEN 
      RAISE_APPLICATION_ERROR(-20101, ERRMSG);
    END IF; 

END;

