-- SANKHYA.TRG_INC_UPD_TGFFIN_CERTIFIC
CREATE OR REPLACE TRIGGER SANKHYA.TRG_INC_UPD_TGFFIN_CERTIFIC
TRG_INC_UPD_TGFFIN_CERTIFIC BEFORE INSERT OR UPDATE ON TGFFIN FOR EACH ROW

DECLARE
  P_OQUE           VARCHAR2(100);
  P_QUEM           VARCHAR2(100);
  P_COM            VARCHAR2(100);
  P_ERRO           VARCHAR2(4);
  P_TIPO           TGFCER.TIPO%TYPE; 
  P_SEQUENCIA      TGFCER.SEQUENCIA%TYPE; 
  P_CHAVE          TGFCER.CHAVE%TYPE; 
  P_CODREGRA       TGFREG.CODREGRA%TYPE; 
  P_INSTPRINC      TGFREG.INSTPRINC%TYPE; 
  P_INSTSEC        TGFREG.INSTSEC%TYPE;
  P_DESCRREGRA     TGFREG.DESCRREGRA%TYPE;
  P_TIPOREGRA      TGFREG.TIPO%TYPE; 
  P_CODINSTPRINC   NUMBER(10,0);
  P_CODINSTSEC     NUMBER(10,0);
  P_COUNT          NUMBER(10,0);
  P_RATEIO         NUMBER(10,0) NULL;
  ERRMSG            VARCHAR2(4000);
  ERROR             EXCEPTION;
  P_VALIDAR BOOLEAN;
  P_CODTIPTIT      TGFTIT.CODTIPTIT%TYPE;

  CURSOR  curRegras IS 
    SELECT CER.TIPO, CER.CHAVE, CER.CODREGRA, CER.SEQUENCIA, REG.INSTPRINC, REG.INSTSEC, REG.TIPO, REG.DESCRREGRA, :NEW.CODTIPTIT
    FROM TGFCER CER
    , TGFREG REG
    WHERE ((CER.TIPO = 'U' AND CER.CHAVE = (CASE WHEN :NEW.DHBAIXA IS NULL  OR :NEW.DHBAIXA = NVL(:OLD.DHBAIXA, ADD_MONTHS(SYSDATE,-1200)) THEN STP_GET_CODUSULOGADO ELSE :NEW.CODUSUBAIXA END)) OR
        (CER.TIPO = 'V' AND CER.CHAVE = :NEW.CODVEND) OR
        (CER.TIPO = 'E' AND CER.CHAVE = :NEW.CODEMP) OR
        (CER.TIPO = 'G'))
    AND CER.ATIVO = 'S'
    AND CER.CODREGRA = REG.CODREGRA
    AND REG.INSTPRINC NOT IN ('Local', 'Tipo de Negociação', 'Produto', 'Grupo de Produto', 'Departamento')
    AND (REG.INSTSEC IS NULL OR REG.INSTSEC NOT IN ('Local', 'Tipo de Negociação', 'Produto', 'Grupo de Produto', 'Departamento'))
    ORDER BY CER.SEQUENCIA;
BEGIN
   
  -- VERIFICANDO SE O CLIENTE UTILIZA ESTA FUNCIONALIDADE
  IF VARIAVEIS_PKG.V_UTILIZA_CERTIFIC = 'N' THEN
    RETURN;
  END IF;

  IF STP_GET_ATUALIZANDO THEN
    RETURN;
  END IF;
  
  /* 
  sincronização de dados
  */
  SELECT COUNT(1) INTO P_COUNT
  FROM TSIPAR
  WHERE CHAVE = 'SBPARCEIRO';

  IF (P_COUNT <> 0) THEN
    RETURN;
  END IF;
  
  P_VALIDAR := Fpodevalidar('TGFFIN');
  IF INSERTING OR UPDATING('CODUSU') OR UPDATING('CODEMP') OR UPDATING('CODVEND') OR
    UPDATING('CODCENCUS') OR UPDATING('CODNAT') OR UPDATING('CODPROJ') OR UPDATING('CODUSUBAIXA') OR
    UPDATING('CODTIPOPER') OR UPDATING('CODTIPOPERBAIXA') OR UPDATING('CODPARC') OR UPDATING('CODCTABCOINT') OR 
    UPDATING('CODTIPTIT') THEN
    OPEN curRegras;
    LOOP
      FETCH curRegras INTO 
         P_TIPO, P_CHAVE, P_CODREGRA, P_SEQUENCIA, P_INSTPRINC, P_INSTSEC, P_TIPOREGRA, P_DESCRREGRA, P_CODTIPTIT;
      EXIT WHEN curRegras%NOTFOUND;
    
      IF P_INSTPRINC = 'Centro de Resultado' THEN
        P_CODINSTPRINC := :NEW.CODCENCUS; 
      ELSIF P_INSTPRINC = 'Natureza' THEN
        P_CODINSTPRINC := :NEW.CODNAT; 
      ELSIF P_INSTPRINC = 'Projeto' THEN
        P_CODINSTPRINC := :NEW.CODPROJ; 
      ELSIF P_INSTPRINC = 'TOP' THEN
      IF :NEW.DHBAIXA IS NULL OR (:NEW.PROVISAO = 'S' AND :NEW.CODTIPOPERBAIXA = 0) THEN /* PROVISOES NAO TEM TOP DE BAIXA, ESSE TRECHO NO SQLSERVER ESTA DIRETO NO CURSOR */    
          P_CODINSTPRINC := :NEW.CODTIPOPER;
        ELSE
          P_CODINSTPRINC := :NEW.CODTIPOPERBAIXA;
        END IF;
      ELSIF (P_INSTPRINC = 'Conta Bancária') OR (P_INSTPRINC = 'Conta Bancaria') THEN
        P_CODINSTPRINC := :NEW.CODCTABCOINT; 
      ELSIF P_INSTPRINC = 'Parceiro' THEN
        P_CODINSTPRINC := :NEW.CODPARC; 
      ELSIF P_INSTPRINC = 'Empresa' THEN
        P_CODINSTPRINC := :NEW.CODEMP;
	  ELSIF P_INSTPRINC = 'Empresa Pessoal' THEN
        P_CODINSTPRINC := :NEW.CODEMP;
      ELSIF P_INSTPRINC = 'Tipo de Título p/ FastService' THEN
        P_CODINSTPRINC := P_CODTIPTIT;
      ELSE
        CLOSE curRegras;
      ERRMSG := 'Instrução principal '|| P_INSTPRINC ||' da regra ' || P_DESCRREGRA || ' não definida.';
      RAISE ERROR;
      END IF;
   
      IF P_INSTSEC = 'Centro de Resultado' THEN
        P_CODINSTSEC := :NEW.CODCENCUS; 
      ELSIF P_INSTSEC = 'Natureza' THEN
        P_CODINSTSEC := :NEW.CODNAT; 
      ELSIF P_INSTSEC = 'Projeto' THEN
        P_CODINSTSEC := :NEW.CODPROJ; 
      ELSIF P_INSTSEC = 'TOP' THEN
      IF :NEW.DHBAIXA IS NULL OR (:NEW.PROVISAO = 'S' AND :NEW.CODTIPOPERBAIXA = 0) THEN /* PROVISOES NAO TEM TOP DE BAIXA, ESSE TRECHO NO SQLSERVER ESTA DIRETO NO CURSOR*/    
          P_CODINSTSEC := :NEW.CODTIPOPER;
        ELSE
          P_CODINSTSEC := :NEW.CODTIPOPERBAIXA;
        END IF;
      ELSIF (P_INSTSEC = 'Conta Bancária') OR (P_INSTSEC = 'Conta Bancaria') THEN
        P_CODINSTSEC := :NEW.CODCTABCOINT; 
      ELSIF P_INSTSEC = 'Parceiro' THEN
        P_CODINSTSEC := :NEW.CODPARC;
      ELSIF P_INSTSEC = 'Empresa' THEN
        P_CODINSTSEC := :NEW.CODEMP;
      ELSIF P_INSTSEC IS NOT NULL AND P_INSTSEC <> '' THEN
        CLOSE curRegras;
      ERRMSG := 'Instrução secundária '|| P_INSTSEC ||' da regra ' || P_DESCRREGRA || ' não definida.';
      RAISE ERROR;
      ELSE
        P_CODINSTSEC := 0;
      END IF;

      IF trim(P_INSTSEC) IS NULL THEN -- Apenas uma dimensão
        SELECT COUNT(*)
        INTO P_COUNT
        FROM TGFITR ITR
        WHERE ITR.CODREGRA = P_CODREGRA
        AND ITR.CODINSTPRINC <= P_CODINSTPRINC
        AND NVL(ITR.CODINSTPRINCFIN, ITR.CODINSTPRINC) >= P_CODINSTPRINC
        AND ITR.ATIVO = 'S';
      ELSE
        SELECT COUNT(*)
        INTO P_COUNT
        FROM TGFITR ITR
        WHERE ITR.CODREGRA = P_CODREGRA
        AND ITR.CODINSTPRINC <= P_CODINSTPRINC
        AND NVL(ITR.CODINSTPRINCFIN, ITR.CODINSTPRINC) >= P_CODINSTPRINC
        AND ITR.CODINSTSECINI <= P_CODINSTSEC  
        AND ITR.CODINSTSECFIN >= P_CODINSTSEC  
        AND ITR.ATIVO = 'S';
      END IF;
      P_ERRO := '';
      IF P_TIPOREGRA = 'R' THEN
        IF P_COUNT > 0 THEN
          P_ERRO := 'ERRO';
        END IF;
      ELSE
        IF P_COUNT = 0 THEN
          P_ERRO := 'ERRO';
        END IF;
      END IF;
       
      IF P_ERRO = 'ERRO' AND :NEW.CODCTABCOINT <> 0 THEN
        CLOSE curRegras;

        Stp_Msg_Certific_Tipo(P_TIPO, P_CHAVE, P_QUEM);
        Stp_Msg_Certific_Codinst(P_INSTPRINC, P_CODINSTPRINC, P_OQUE);

        IF trim(P_INSTSEC) IS NULL THEN
        ERRMSG := P_QUEM ||' não pode usar '|| P_OQUE ||' da regra '||P_DESCRREGRA||'\'||P_SEQUENCIA||'.';
        RAISE ERROR;
        ELSE
          Stp_Msg_Certific_Codinst(P_INSTSEC, P_CODINSTSEC, P_COM);
        ERRMSG := P_QUEM ||' não pode usar '|| P_OQUE ||' com '|| P_COM ||' da regra '||P_DESCRREGRA||'\'||P_SEQUENCIA||'.';
        RAISE ERROR;
        END IF;   
      END IF;
    END LOOP;
    CLOSE curRegras;
  END IF;
  
  RETURN;
      
EXCEPTION
  WHEN OTHERS THEN
    /* 
    Sincronização de dados não faz validações
    */
     IF (P_VALIDAR) THEN
       IF SQLCODE <> 1 THEN
         ERRMSG := ERRMSG || '  ' || SQLERRM;
       END IF;
       RAISE_APPLICATION_ERROR(-20101, ERRMSG);       
     END IF;
END;

