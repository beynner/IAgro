-- SANKHYA.TRG_INC_UPD_TGFITE_TRANSG
CREATE OR REPLACE TRIGGER SANKHYA.TRG_INC_UPD_TGFITE_TRANSG
"SANKHYA".TRG_INC_UPD_TGFITE_TRANSG
AFTER INSERT OR UPDATE ON TGFITE 
FOR EACH ROW

DECLARE
  P_DTNEG          TGFCAB.DTNEG%TYPE;
  P_NUMNOTA        TGFCAB.NUMNOTA%TYPE;
  P_CODPARC        TGFCAB.CODPARC%TYPE;
  P_NUMCONTRATO    TGFCAB.NUMCONTRATO%TYPE;
  P_ATUALTRANSG    TGFTOP.ATUALTRANSG%TYPE;
  P_DECLARADO      TGAGTM.DECLARADO%TYPE;
  P_CODSAF         TGAGTM.CODSAF%TYPE;
  P_QTDCALC        TGAGTM.QTDCALC%TYPE;
  P_QTDROYALT      TGAGTM.QTDROYALT%TYPE;
  P_SALDO          TGAGTS.SALDO%TYPE;
  P_GRUPOTRANSG    TGFPRO.GRUPOTRANSG%TYPE;
  P_CREDITO        TGAGT.CREDITO%TYPE;
  P_NUTRS          TGAGTM.NUTRS%TYPE;
  P_QTDEMB         TGFPRO.QTDEMB%TYPE;
  P_QTDNEG         TGFITE.QTDNEG%TYPE;
  P_COUNT          INT;
  P_VALIDAR       BOOLEAN;    
  
BEGIN

  -- VERIFICANDO SE O CLIENTE UTILIZA ESTA FUNCIONALIDADE
  SELECT COUNT(*) INTO P_COUNT
  FROM TSIVARBD
  WHERE UTILIZA_TRANSG = 'N';
  IF P_COUNT > 0 THEN 
    RETURN;
  END IF;

  IF STP_GET_ATUALIZANDO THEN
    RETURN;
  END IF;

  P_VALIDAR := Fpodevalidar('TGFITE');
  
  IF :OLD.GRUPOTRANSG IS NOT NULL THEN
    SELECT COUNT(1)
      INTO P_COUNT
    FROM TGAGTM
    WHERE NUNOTA = :OLD.NUNOTA
    AND SEQUENCIA = :OLD.SEQUENCIA;
    
    IF P_COUNT > 0 THEN
      DELETE FROM TGAGTM
      WHERE NUNOTA = :OLD.NUNOTA
      AND SEQUENCIA = :OLD.SEQUENCIA;
    END IF;
  END IF;    

  IF :NEW.GRUPOTRANSG IS NOT NULL THEN
   BEGIN
    SELECT CAB.DTNEG, 
           CAB.NUMNOTA, 
           CAB.CODPARC, 
           CAB.NUMCONTRATO, 
           TPO.ATUALTRANSG, 
           CON.CODSAF
      INTO P_DTNEG, 
           P_NUMNOTA, 
           P_CODPARC, 
           P_NUMCONTRATO, 
           P_ATUALTRANSG, 
           P_CODSAF
      FROM TGFCAB CAB
         , TGFTOP TPO
         , TCSCON CON
     WHERE CAB.NUNOTA = :NEW.NUNOTA
        AND CAB.CODTIPOPER = TPO.CODTIPOPER
        AND CAB.DHTIPOPER = TPO.DHALTER
        AND CAB.NUMCONTRATO = CON.NUMCONTRATO
        AND CON.CODSAF IS NOT NULL;   
   EXCEPTION WHEN NO_DATA_FOUND THEN
        IF (NOT P_VALIDAR) THEN
          RETURN;
        ELSE
          RAISE_APPLICATION_ERROR(-20101, 'Contrato nao cadastrado na TCSCON ou contrato sem o codigo de safra definido para transgenicos.');
        END IF;
      END; 
   
    IF P_ATUALTRANSG = 0 THEN
      RETURN;
    END IF;
   
    P_DECLARADO := 'S';
    IF P_ATUALTRANSG = 1 THEN 
      SELECT CREDITO 
      INTO P_CREDITO
      FROM TGAGT GT
      WHERE GRUPOTRANSG = :NEW.GRUPOTRANSG;
       
    SELECT QTDEMB
    INTO P_QTDEMB
    FROM TGFPRO
    WHERE CODPROD = :NEW.CODPROD;
    
    IF P_QTDEMB IS NULL OR P_QTDEMB = 0 THEN
        IF (NOT P_VALIDAR) THEN
          RETURN;
        ELSE
          RAISE_APPLICATION_ERROR(-20101, 'Qtde Embalagens deve ser informada para Crédito de Transgênico.');
        END IF;
      END IF;
    
      P_QTDNEG := :NEW.QTDNEG * P_QTDEMB;
      P_QTDCALC := :NEW.QTDNEG * P_CREDITO * P_QTDEMB;
      P_QTDROYALT := 0;
    ELSE
      SELECT GRUPOTRANSG
      INTO P_GRUPOTRANSG
      FROM TGAGTP
      WHERE CODPROD = :NEW.CODPROD
    AND CONTROLE = :NEW.CONTROLE;

      IF P_GRUPOTRANSG IS NULL THEN
        P_DECLARADO := 'N';
      END IF;
      SELECT SALDO 
      INTO P_SALDO
      FROM TGAGTS
      WHERE GRUPOTRANSG = :NEW.GRUPOTRANSG
      AND CODSAF = P_CODSAF 
      AND CODPARC = P_CODPARC;
      IF P_SALDO >= :NEW.QTDNEG THEN
        P_QTDCALC := :NEW.QTDNEG;
        P_QTDROYALT := 0;
      ELSE
        P_QTDCALC := P_SALDO;
        P_QTDROYALT := :NEW.QTDNEG - P_SALDO;
      END IF;
      P_QTDNEG := :NEW.QTDNEG;
      
    END IF;
   
    SELECT NVL(MAX(NUTRS),0)+1
    INTO P_NUTRS
    FROM TGAGTM;
   
    INSERT INTO TGAGTM 
      (NUTRS, 
       GRUPOTRANSG, 
       CODSAF, 
       CODPARC, 
       CODUSU, 
       DTALTER, 
       NUMCONTRATO, 
       SINAL, 
       DECLARADO, 
       DIGITADO, 
       NUNOTA, 
       SEQUENCIA, 
       DTNEG, 
       NUMNOTA, 
       QTDNEG, 
       QTDCALC, 
       QTDROYALT, 
       DTVAL)
    VALUES
      (P_NUTRS, 
      :NEW.GRUPOTRANSG, 
      P_CODSAF, 
      P_CODPARC, 
      :NEW.CODUSU, 
      :NEW.DTALTER, 
      P_NUMCONTRATO, 
      P_ATUALTRANSG, 
      P_DECLARADO, 
      'N', 
      :NEW.NUNOTA, 
      :NEW.SEQUENCIA, 
      P_DTNEG, 
      P_NUMNOTA, 
      P_QTDNEG, 
      P_QTDCALC, 
      P_QTDROYALT, 
      NULL);
  END IF;
END;

