-- SANKHYA.TRG_INC_UPD_FX_TGFCAB
CREATE OR REPLACE TRIGGER SANKHYA.TRG_INC_UPD_FX_TGFCAB
"SANKHYA".TRG_INC_UPD_FX_TGFCAB
	BEFORE INSERT OR UPDATE
	ON TGFCAB 
	FOR EACH ROW 

DECLARE
	P_TEM_CHECKOUT	BOOLEAN;
	P_NUMROWS		INT;
	P_VALIDAR		BOOLEAN;
	P_CODMODDOC 	NUMBER;	
	P_COUNTROWS		INT;
	ERRMSG			VARCHAR(255);
	ERROR           EXCEPTION;
BEGIN
	IF STP_GET_ATUALIZANDO THEN
		RETURN;
	END IF;
	
	P_TEM_CHECKOUT := FCheckout_Utiliza;
	IF (NOT P_TEM_CHECKOUT) THEN
		RETURN;
	END IF;
	
	P_VALIDAR := Fpodevalidar ('TGFCAB');
	IF (NOT P_VALIDAR)
	THEN
		RETURN;
	END IF;
	
	SELECT CODMODDOC INTO P_CODMODDOC
	FROM TGFTOP
	WHERE CODTIPOPER = :NEW.CODTIPOPER
		AND DHALTER = :NEW.DHTIPOPER;
	
	IF(NVL(P_CODMODDOC,0) <> 65) THEN
		RETURN;
	END IF;
	
	IF(NVL(:NEW.CODCHECKOUT,0) <> 0) THEN
		RETURN;
	END IF;
	
	SELECT COUNT(1) INTO P_COUNTROWS
	FROM TFXPDV
	WHERE SERIE = :NEW.SERIENOTA
		AND CODEMPRESA = :NEW.CODEMP;
	IF(P_COUNTROWS > 0) THEN
		ERRMSG := 'Não é possível inserir uma NFCe com série e cód.empresa que já estejam sendo utilizados por um Checkout';
		RAISE ERROR;
	ELSE 
		RETURN;
	END IF;
EXCEPTION
  WHEN ERROR THEN
    IF (P_VALIDAR) THEN 
      RAISE_APPLICATION_ERROR(-20101, ERRMSG);
    END IF;  
END;

