-- SANKHYA.TRG_DLT_TGFCAC_FLEX
CREATE OR REPLACE TRIGGER SANKHYA.TRG_DLT_TGFCAC_FLEX
"SANKHYA".TRG_DLT_TGFCAC_FLEX 
BEFORE DELETE ON TGFCAC FOR EACH ROW

DECLARE
  P_CODPARC          TGFCAB.CODPARC%TYPE;
  P_CODVEND          TGFCAB.CODVEND%TYPE;
  P_CODVENDRETEN     TGFCAB.CODVEND%TYPE;
  P_GRUPORETENCAO    TGFVEN.GRUPORETENCAO%TYPE;
  P_ATUALACDC        TGFTOP.ATUALACDC%TYPE;
  P_TIPMOV           TGFCAB.TIPMOV%TYPE;
  P_VLRACRESCDESC    TGFITE.VLRUNIT%TYPE;
  P_VLRRETENCAO      TGFITE.VLRUNIT%TYPE;  
  P_RETDESCDEV       CHAR(1) := 'S';
  P_ACRESCSEMCONF    CHAR(1) := 'N';
  P_COUNT            NUMBER(05);
BEGIN

  IF STP_GET_ATUALIZANDO THEN
    RETURN;
  END IF;

  BEGIN
    SELECT CAB.CODVEND, TOP.ATUALACDC, CAB.TIPMOV, CAB.CODPARC
    INTO  P_CODVEND, P_ATUALACDC, P_TIPMOV, P_CODPARC
    FROM TGFCAB CAB, TGFTOP TOP
    WHERE CAB.NUNOTA = :OLD.NUNOTA
    AND CAB.CODTIPOPER = TOP.CODTIPOPER
    AND CAB.DHTIPOPER = TOP.DHALTER;
  EXCEPTION WHEN NO_DATA_FOUND THEN
    P_CODVEND := Tgfcab_Pkg.V_CODVEND(1);
    P_TIPMOV  :=  Tgfcab_Pkg.V_TIPMOV(1);
    P_CODPARC  :=  Tgfcab_Pkg.V_CODPARC(1);
    SELECT TOP.ATUALACDC
    INTO  P_ATUALACDC
    FROM TGFTOP TOP
    WHERE TOP.CODTIPOPER = Tgfcab_Pkg.V_CODTIPOPER(1)
    AND TOP.DHALTER = Tgfcab_Pkg.V_DHTIPOPER(1);
  END;
  
  IF P_ATUALACDC = 'N' THEN 
    RETURN;
  END IF;

  SELECT GRUPORETENCAO
  INTO P_GRUPORETENCAO
  FROM TGFVEN
  WHERE CODVEND = P_CODVEND;
  IF P_GRUPORETENCAO IS NULL THEN
    RETURN;
  END IF;
  
  IF P_TIPMOV = 'D' THEN
    IF (P_GRUPORETENCAO <> 'PARCEIRO' OR :OLD.VLRACRESCDESC > 0) THEN
      RETURN;
    END IF;

    BEGIN
      SELECT LOGICO 
      INTO P_RETDESCDEV
      FROM TSIPAR 
      WHERE CHAVE = 'RETDESCDEV';
    EXCEPTION WHEN NO_DATA_FOUND THEN
      P_RETDESCDEV := 'S';
    END;

    IF P_RETDESCDEV = 'S' THEN
      RETURN;
    END IF;
  END IF;

  P_VLRRETENCAO := NVL(:OLD.VLRRETENCAO,0);
  P_VLRACRESCDESC := NVL(:OLD.VLRACRESCDESC,0);
  IF P_VLRACRESCDESC < 0 THEN

    IF (P_ATUALACDC = 'S' OR P_TIPMOV <> 'D') THEN
      IF P_GRUPORETENCAO = 'PARCEIRO' THEN
        IF (P_TIPMOV <> 'D' OR P_RETDESCDEV = 'N') THEN
          UPDATE TGFPAR 
          SET SALDODISPCAC = NVL(SALDODISPCAC, 0) - P_VLRACRESCDESC
          WHERE CODPARC = P_CODPARC;        
        END IF;
      ELSE
        UPDATE TGFVEN 
        SET SALDODISPCAC = NVL(SALDODISPCAC, 0) - P_VLRACRESCDESC
        WHERE CODVEND = P_CODVEND;
      END IF;
    END IF;
  ELSIF P_VLRACRESCDESC > 0 AND P_TIPMOV <> 'D' THEN  
    P_VLRACRESCDESC := (P_VLRACRESCDESC - P_VLRRETENCAO);

    IF (P_ATUALACDC = 'S') THEN    
      BEGIN
        SELECT LOGICO 
        INTO P_ACRESCSEMCONF
        FROM TSIPAR
        WHERE CHAVE = 'ACRESCSEMCONF';
      EXCEPTION WHEN NO_DATA_FOUND THEN
        P_ACRESCSEMCONF := 'N';
      END;
      IF (P_ACRESCSEMCONF = 'S') THEN
        IF P_VLRRETENCAO <> 0 THEN
          BEGIN
            SELECT INTEIRO 
            INTO P_CODVENDRETEN 
            FROM TSIPAR 
            WHERE CHAVE = 'CODVENDACUVLRRE';
          EXCEPTION WHEN NO_DATA_FOUND THEN
            P_CODVENDRETEN := 0;
          END;
          IF P_CODVENDRETEN > 0 THEN
            UPDATE TGFVEN
            SET SALDODISPCAC = NVL(SALDODISPCAC, 0) - P_VLRRETENCAO
            WHERE CODVEND = P_CODVENDRETEN;
          END IF;
        END IF;
        IF P_GRUPORETENCAO = 'PARCEIRO' THEN            
          UPDATE TGFPAR
          SET SALDODISPCAC = NVL(SALDODISPCAC, 0) - P_VLRACRESCDESC
          WHERE CODPARC = P_CODPARC;
        ELSE
          UPDATE TGFVEN
          SET SALDODISPCAC = NVL(SALDODISPCAC, 0) - P_VLRACRESCDESC
          WHERE CODVEND = P_CODVEND;
        END IF;
      ELSE
        IF P_GRUPORETENCAO = 'PARCEIRO' THEN
          UPDATE TGFPAR
          SET PROVACRESCCAC = NVL(PROVACRESCCAC, 0) - P_VLRACRESCDESC
          WHERE CODPARC = P_CODPARC;
        ELSE
          UPDATE TGFVEN
          SET PROVACRESCCAC = NVL(PROVACRESCCAC, 0) - P_VLRACRESCDESC
          WHERE CODVEND = P_CODVEND;
        END IF;
      END IF;      
    ELSIF P_ATUALACDC = 'P' THEN
      IF P_GRUPORETENCAO = 'PARCEIRO' THEN
        UPDATE TGFPAR
        SET PROVACRESCCAC = NVL(PROVACRESCCAC, 0) - P_VLRACRESCDESC
        WHERE CODPARC = P_CODPARC;
      ELSE
        UPDATE TGFVEN
        SET PROVACRESCCAC = NVL(PROVACRESCCAC, 0) - P_VLRACRESCDESC
        WHERE CODVEND = P_CODVEND;
      END IF;
    END IF;    
  END IF;
END;

