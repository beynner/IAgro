-- SANKHYA.TRG_INC_UPD_TGFITE_RASTEST
CREATE OR REPLACE TRIGGER SANKHYA.TRG_INC_UPD_TGFITE_RASTEST
TRG_INC_UPD_TGFITE_RASTEST
   BEFORE INSERT OR UPDATE
   ON TGFITE
   FOR EACH ROW

DECLARE
   P_QTDDISP                 FLOAT;
   P_QTD                     FLOAT;
   P_NUNOTA_ITS              NUMBER (10);
   P_SEQUENCIA_ITS           NUMBER (10);
   P_TIPMOV                  CHAR (1);
   P_CODTIPOPER              NUMBER (10);
   P_DHTIPOPER               DATE;
   P_OBSPADRASTEST           VARCHAR (4000);
   P_BCICMSSTANT             FLOAT;
   P_VLROUTROS               FLOAT;
   P_VLRICMSANT              FLOAT;
   P_BASESUBSTITANT          FLOAT;
   P_VLRSUBSTANT             FLOAT;
   P_BASESTFCPINTANT         FLOAT;
   P_VLRSTFCPINTANT          FLOAT;
   P_PERCSTFCPINTANT         FLOAT;
   P_GRAVA_VLRICMSANT        FLOAT;
   P_GRAVA_BASESUBSTITANT    FLOAT;
   P_GRAVA_VLRSUBSTANT       FLOAT;
   P_GRAVA_BASESTFCPINTANT   FLOAT;
   P_GRAVA_VLRSTFCPINTANT    FLOAT;
   P_GRAVA_PERCSTFCPINTANT   FLOAT;
   P_ALIQSTFCPSTANT          FLOAT;
   P_ATUALLIVFIS	     CHAR (1);
   P_DTNEG                   DATE;
   P_EMPSTANTPORMED          NUMBER(10);
   P_PRODSTANTPORMED         NUMBER(10);
   P_STANTPORMED             NUMBER(10);
   P_COUNT                   NUMBER(10);
   P_OBTSTANTMEDENT          CHAR(1);
   --BUSCA SALDO DA TGFITS PARA LIGACAO
   CURSOR CUR_TGFITS
   IS
        SELECT I.NUNOTA, I.SEQUENCIA, (I.QTDENT - NVL (I.QTDSAI, 0)) AS QTDDISP
          FROM TGFITS I, TGFPRO P
         WHERE     I.CODPROD = :NEW.CODPROD
               AND P.CODPROD = I.CODPROD
               AND I.CODEMP = :NEW.CODEMP
               AND ( (P.RASTRESTOQUE NOT IN ('C', 'L'))
                    OR (CONTROLE = :NEW.CONTROLE))
               AND ( (P.RASTRESTOQUE NOT IN ('O', 'L'))
                    OR (I.CODLOCAL = :NEW.CODLOCALORIG))
               AND ROUND(QTDENT - NVL(QTDSAI, 0), NVL(DECQTD, 0)) > 0
      ORDER BY I.DTENTSAI, NUNOTA;
BEGIN
    --St anterior por média das entradas  
   SELECT COUNT(1) INTO P_EMPSTANTPORMED FROM TGFEMP WHERE CODEMP = :NEW.CODEMP AND NVL(OBTSTANTMEDENT, 'N') = 'S';
   P_PRODSTANTPORMED := 0;
   IF (P_EMPSTANTPORMED > 0) THEN 
		P_OBTSTANTMEDENT := NULL;
		P_COUNT := 0;
		
		SELECT  MAX(PEM.OBTSTANTMEDENT), COUNT(1)INTO P_OBTSTANTMEDENT, P_COUNT
		FROM TGFPEM PEM
		WHERE
		PEM.CODEMP = :NEW.CODEMP 
		AND PEM.CODPROD = :NEW.CODPROD;		  

		IF P_COUNT = 0 OR P_OBTSTANTMEDENT IS NULL  THEN --SE não tem na TGFPEM ou esta nulo olha no produto
			SELECT 
			NVL(PRO.OBTSTANTMEDENT,'N') INTO P_OBTSTANTMEDENT
			FROM TGFPRO PRO
			WHERE 
			PRO.CODPROD = :NEW.CODPROD;
		END IF;
    END IF;	    
    IF (P_OBTSTANTMEDENT = 'S') THEN
        SELECT DTNEG INTO P_DTNEG FROM TGFCAB WHERE NUNOTA = :NEW.NUNOTA;
        SELECT COUNT(1) INTO P_STANTPORMED
                 FROM TGFSTMM
                 WHERE CODEMP = :NEW.CODEMP
                   AND CODPROD = :NEW.CODPROD
                   AND REFERENCIA = ADD_MONTHS(SNK_TRUNC_DATE(P_DTNEG, 'MM'), -1);
        IF P_STANTPORMED > 0 THEN
            SELECT ROUND(VICMSSTRETUNIT * :NEW.QTDNEG, 2)
                  ,ROUND(BCSTRETUNIT * :NEW.QTDNEG, 2)
              INTO :NEW.VLRSUBSTANT, :NEW.BASESUBSTITANT
              FROM TGFSTMM
             WHERE CODEMP = :NEW.CODEMP
               AND CODPROD = :NEW.CODPROD
               AND REFERENCIA = ADD_MONTHS(SNK_TRUNC_DATE(P_DTNEG, 'MM'), -1);
					 RETURN;
        END IF;
    END IF;

   -- VERIFICANDO SE O CLIENTE UTILIZA ESTA FUNCIONALIDADE
   IF VARIAVEIS_PKG.V_UTILIZA_RASTEST = 'N'
   THEN
      RETURN;
   END IF;
   IF NVL (:OLD.STATUSNOTA, 'A') <> NVL (:NEW.STATUSNOTA, 'A')
   THEN
      IF :NEW.BASESUBSTITANT < 0 OR :NEW.VLRSUBSTANT < 0
      THEN
         :NEW.BASESUBSTITANT := 0;
         :NEW.VLRSUBSTANT := 0;
      END IF;
      --VERIFICA SE EXISTE CONFIGURAO PARA AJUSTES FISCAIS NO RASTREAMENTO
      SELECT TEXTO
        INTO P_OBSPADRASTEST
        FROM TSIPAR
       WHERE CHAVE = 'OBSPADRASTEST';
       
      SELECT CAB.TIPMOV, CAB.CODTIPOPER, CAB.DHTIPOPER, TPO.ATUALLIVFIS
        INTO P_TIPMOV, P_CODTIPOPER, P_DHTIPOPER, P_ATUALLIVFIS
        FROM TGFCAB CAB
        JOIN TGFTOP TPO ON (CAB.CODTIPOPER = TPO.CODTIPOPER AND CAB.DHTIPOPER = TPO.DHALTER)
       WHERE NUNOTA = :NEW.NUNOTA;
	  
		IF (P_ATUALLIVFIS = 'N')
		THEN
			RETURN;
		END IF;
      IF (P_TIPMOV = 'V' OR (P_TIPMOV = 'T' AND :NEW.ATUALESTOQUE < 0))
         AND Get_Tem_Rastreamento_Itens (P_TIPMOV,
                                         :NEW.CODEMP,
                                         P_CODTIPOPER,
                                         P_DHTIPOPER,
                                         :NEW.CODPROD) = 'S'
      THEN
         P_QTD := :NEW.QTDNEG;
         P_GRAVA_VLRICMSANT := 0;
         P_GRAVA_BASESUBSTITANT := 0;
         P_GRAVA_VLRSUBSTANT := 0;
         P_GRAVA_BASESTFCPINTANT := 0;
         P_GRAVA_VLRSTFCPINTANT := 0;
         P_GRAVA_PERCSTFCPINTANT := 0;
         OPEN CUR_TGFITS;
         WHILE P_QTD > 0
         LOOP
            FETCH CUR_TGFITS
            INTO P_NUNOTA_ITS, P_SEQUENCIA_ITS, P_QTDDISP;
		    IF P_QTD > P_QTDDISP
		    THEN
			   GET_IMPOSTOS_RAT_EST (P_NUNOTA_ITS,
									 P_SEQUENCIA_ITS,
									 P_QTDDISP,
									 P_VLRICMSANT,
									 P_BASESUBSTITANT,
									 P_VLRSUBSTANT,
									 P_BASESTFCPINTANT,
									 P_VLRSTFCPINTANT,
									 P_PERCSTFCPINTANT,
									 P_ALIQSTFCPSTANT);
			   P_QTD := P_QTD - P_QTDDISP;
		    ELSE
			   GET_IMPOSTOS_RAT_EST (P_NUNOTA_ITS,
									 P_SEQUENCIA_ITS,
									 P_QTD,
									 P_VLRICMSANT,
									 P_BASESUBSTITANT,
									 P_VLRSUBSTANT,
									 P_BASESTFCPINTANT,
									 P_VLRSTFCPINTANT,
									 P_PERCSTFCPINTANT,
									 P_ALIQSTFCPSTANT);
			   P_QTD := 0;
		    END IF;
		    P_GRAVA_VLRICMSANT := P_GRAVA_VLRICMSANT + P_VLRICMSANT;
		    P_GRAVA_BASESUBSTITANT := P_GRAVA_BASESUBSTITANT + P_BASESUBSTITANT;
		    P_GRAVA_VLRSUBSTANT := P_GRAVA_VLRSUBSTANT + P_VLRSUBSTANT;
		    P_GRAVA_BASESTFCPINTANT := P_GRAVA_BASESTFCPINTANT + P_BASESTFCPINTANT;
		    P_GRAVA_VLRSTFCPINTANT := P_GRAVA_VLRSTFCPINTANT + P_VLRSTFCPINTANT;
		    --VERIFICA SE EXISTE CONFIGURACAO PARA AJUSTES FISCAIS NO RASTREAMENTO
		    SELECT TEXTO
			  INTO P_OBSPADRASTEST
			  FROM TSIPAR
			WHERE CHAVE = 'OBSPADRASTEST';
		    --PEGA VALORES DE AJUSTES FISCAIS E APLICA NO RASTREAMENTO
		    IF P_OBSPADRASTEST <> ''
		    THEN
			   BEGIN
				  SELECT BCICMSSTANT, VLROUTROS
				    INTO P_BCICMSSTANT, P_VLROUTROS
				    FROM TGFAJD
				   WHERE NUNOTA = :NEW.NUNOTA
						 AND SEQUENCIA = :NEW.SEQUENCIA
						 AND CODOBSPADRAO = P_OBSPADRASTEST;
			   EXCEPTION
				  WHEN NO_DATA_FOUND
				  THEN
					 P_BCICMSSTANT := 0;
				 	 P_VLROUTROS := 0;
			   END;
			   P_GRAVA_BASESUBSTITANT := P_GRAVA_BASESUBSTITANT + (P_BASESUBSTITANT + P_BCICMSSTANT);
			   P_GRAVA_VLRSUBSTANT := P_GRAVA_VLRSUBSTANT + (P_VLRSUBSTANT + P_VLROUTROS);
		    END IF; 
         END LOOP;
         CLOSE CUR_TGFITS;
         IF P_GRAVA_BASESUBSTITANT > 0 THEN
            IF(P_GRAVA_VLRICMSANT < 0) THEN
                P_GRAVA_VLRICMSANT := 0;
            END IF;
            IF(P_GRAVA_BASESUBSTITANT < 0) THEN
                P_GRAVA_BASESUBSTITANT := 0;
            END IF;
            IF(P_GRAVA_VLRSUBSTANT < 0) THEN
                P_GRAVA_VLRSUBSTANT := 0;
            END IF;
            IF(P_GRAVA_BASESTFCPINTANT < 0) THEN
                P_GRAVA_BASESTFCPINTANT := 0;
            END IF;
            IF(P_GRAVA_VLRSTFCPINTANT < 0) THEN
                P_GRAVA_VLRSTFCPINTANT := 0;
            END IF;
            IF(P_PERCSTFCPINTANT < 0) THEN
                P_PERCSTFCPINTANT := 0;
            END IF;
            IF(P_ALIQSTFCPSTANT < 0) THEN
                P_ALIQSTFCPSTANT := 0;
            END IF;
            :NEW.VLRICMSANT := P_GRAVA_VLRICMSANT;
            :NEW.BASESUBSTITANT := P_GRAVA_BASESUBSTITANT;
            :NEW.VLRSUBSTANT := P_GRAVA_VLRSUBSTANT;
            :NEW.BASESTFCPINTANT := P_GRAVA_BASESTFCPINTANT;
            :NEW.VLRSTFCPINTANT := P_GRAVA_VLRSTFCPINTANT;
            :NEW.PERCSTFCPINTANT := P_PERCSTFCPINTANT;
            :NEW.ALIQSTFCPSTANT := P_ALIQSTFCPSTANT;
         END IF;
      END IF;
   END IF;
END;

