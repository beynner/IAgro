-- SANKHYA.TRG_INC_UPD_TGFCAB_RASTST
CREATE OR REPLACE TRIGGER SANKHYA.TRG_INC_UPD_TGFCAB_RASTST
"SANKHYA".TRG_INC_UPD_TGFCAB_RASTST
BEFORE INSERT OR UPDATE OR DELETE
ON TGFCAB
FOR EACH ROW

DECLARE P_COUNT        NUMBER(10);
BEGIN
    IF STP_GET_ATUALIZANDO THEN 
      RETURN;
    END IF;

    IF :NEW.TIPMOV = 'T' AND NVL(:NEW.STATUSNFE, '*') = 'A' AND NVL(:NEW.STATUSNFE, '*') <> NVL(:OLD.STATUSNFE, '*') THEN
        FOR ITE IN (SELECT CODPROD
                         , SEQUENCIA
                         , ROUND(VLRICMSANT / I.QTDNEG, 2) AS VLRICMSANT
                         , ROUND(BASESUBSTITANT / I.QTDNEG, 2) AS BASESUBSTITANT
                         , ROUND(VLRSUBSTANT / I.QTDNEG, 2) AS VLRSUBSTANT
                         , ALIQSTFCPSTANT
                         , ROUND(BASESTFCPINTANT / I.QTDNEG, 2) AS BASESTFCPINTANT
                         , ROUND(VLRSTFCPINTANT / I.QTDNEG, 2) AS VLRSTFCPINTANT
                         , PERCSTFCPINTANT
                    FROM TGFITE I
                    WHERE I.NUNOTA = :NEW.NUNOTA
                      AND I.ATUALESTOQUE = -1
                      AND NVL(BASESUBSTITANT, 0) > 0
                      AND NVL(GET_TEM_RASTSTULTENTRADA(:NEW.CODEMPNEGOC, I.CODPROD, :NEW.CODTIPOPER, :NEW.DHTIPOPER), 'S') = 'S') LOOP
            SELECT COUNT(1) INTO P_COUNT
            FROM TGFCST
            WHERE CODEMP = :NEW.CODEMPNEGOC
              AND CODPROD = ITE.CODPROD;
            
            IF P_COUNT > 0 THEN
                UPDATE TGFCST SET NUNOTAENTRADA = :NEW.NUNOTA
                                , SEQUENCIAENTRADA = ITE.SEQUENCIA
                                , VLRICMSUNIT = ITE.VLRICMSANT
                                , BASESUBSTUNIT = ITE.BASESUBSTITANT
                                , VLRSUBSTUNIT = ITE.VLRSUBSTANT
                                , PERCSUBST = ITE.ALIQSTFCPSTANT
                                , BASESTFCPINTANTUNIT = ITE.BASESTFCPINTANT
                                , VLRSTFCPINTANTUNIT = ITE.VLRSTFCPINTANT
                                , PERCSTFCPINTANTUNIT = ITE.PERCSTFCPINTANT
                                , DIGITADO = 'N'
                WHERE CODEMP = :NEW.CODEMPNEGOC
                    AND CODPROD = ITE.CODPROD;
            ELSE
                INSERT INTO TGFCST(CODEMP, CODPROD, NUNOTAENTRADA, SEQUENCIAENTRADA, VLRICMSUNIT, BASESUBSTUNIT, VLRSUBSTUNIT, PERCSUBST, BASESTFCPINTANTUNIT, VLRSTFCPINTANTUNIT, PERCSTFCPINTANTUNIT, DIGITADO)
                            VALUES(:NEW.CODEMPNEGOC, ITE.CODPROD, :NEW.NUNOTA, ITE.SEQUENCIA, ITE.VLRICMSANT, ITE.BASESUBSTITANT, ITE.VLRSUBSTANT, ITE.ALIQSTFCPSTANT, ITE.BASESTFCPINTANT, ITE.VLRSTFCPINTANT, ITE.PERCSTFCPINTANT, 'N');
            END IF;
        END LOOP;                  
    END IF;
END;

