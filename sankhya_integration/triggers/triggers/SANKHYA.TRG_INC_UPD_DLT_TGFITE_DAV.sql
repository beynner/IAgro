-- SANKHYA.TRG_INC_UPD_DLT_TGFITE_DAV
CREATE OR REPLACE TRIGGER SANKHYA.TRG_INC_UPD_DLT_TGFITE_DAV
"SANKHYA".TRG_INC_UPD_DLT_TGFITE_DAV
AFTER DELETE OR INSERT OR UPDATE ON TGFITE
FOR EACH ROW

DECLARE P_COUNT           NUMBER(10);
        P_BASENUMERACAO   CHAR(1);
        P_STATUSNOTA      CHAR(1);
        P_NEWSEQUENCIA    NUMBER(10);
        P_OLDSEQUENCIA    NUMBER(10);
BEGIN


  -- VERIFICANDO SE O CLIENTE UTILIZA ESTA FUNCIONALIDADE
  SELECT COUNT(*) INTO P_COUNT
  FROM TSIVARBD
  WHERE UTILIZA_DAV = 'N';
  IF P_COUNT > 0 THEN 
    RETURN;
  END IF;

  /* QDO SUBSTITUINDO PRODUTO, N?O VALIDAR ESTOQUE */
  IF (VARIAVEIS_PKG.V_SBPRODUTO) THEN
    RETURN;
  END IF;
  
  SELECT COUNT(1) INTO P_COUNT
  FROM TSIPAR
  WHERE CHAVE = 'SBPRODUTO';
  IF P_COUNT <> 0 THEN 
    RETURN;
  END IF;
  
  IF DELETING THEN
    SELECT COUNT(1) INTO P_COUNT
    FROM TGFCAB
    WHERE NUNOTA = :OLD.NUNOTA;
    
    IF P_COUNT > 0 THEN
      SELECT CASE WHEN CAB.TIPMOV = 'P' THEN TPO.BASENUMERACAO ELSE 'N' END
           ,CAB.STATUSNOTA
        INTO P_BASENUMERACAO, P_STATUSNOTA
      FROM TGFTOP TPO
         , TGFCAB CAB
      WHERE CAB.NUNOTA = :OLD.NUNOTA
        AND TPO.CODTIPOPER = CAB.CODTIPOPER
        AND TPO.DHALTER = CAB.DHTIPOPER;      
    ELSE
      BEGIN
        SELECT CASE WHEN CAB.TIPMOV = 'P' THEN TPO.BASENUMERACAO ELSE 'N' END
           ,CAB.STATUSNOTA
        INTO P_BASENUMERACAO, P_STATUSNOTA
        FROM TGFTOP TPO
           , TGFCAB_EXC CAB
        WHERE CAB.NUNOTA = :OLD.NUNOTA
          AND TPO.CODTIPOPER = CAB.CODTIPOPER
          AND TPO.DHALTER = CAB.DHTIPOPER;
      EXCEPTION WHEN NO_DATA_FOUND THEN
        RETURN;
      END;            
    END IF;
  ELSIF UPDATING AND
  (:NEW.NUNOTA            <> :OLD.NUNOTA OR
   :NEW.SEQUENCIA         <> :OLD.SEQUENCIA OR    
    NVL(:NEW.CODPROD,0)   <> NVL(:OLD.CODPROD,0) OR
    NVL(:NEW.QTDNEG,0)    <> NVL(:OLD.QTDNEG,0) OR
    NVL(:NEW.CODVOL,0)    <> NVL(:OLD.CODVOL,0) OR
    NVL(:NEW.VLRCUS,0)    <> NVL(:OLD.VLRCUS,0) OR
    NVL(:NEW.VLRUNIT,0)   <> NVL(:OLD.VLRUNIT,0) OR
    NVL(:NEW.VLRDESC,0)   <> NVL(:OLD.VLRDESC,0) OR
    NVL(:NEW.VLRTOT,0)    <> NVL(:OLD.VLRTOT,0) OR
    NVL(:NEW.CODTRIB,0)   <> NVL(:OLD.CODTRIB,0)OR
    NVL(:NEW.ALIQICMS,0)  <> NVL(:OLD.ALIQICMS,0) OR
    NVL(:NEW.CANCELADO,0) <> NVL(:OLD.CANCELADO,0)) THEN
    BEGIN
      SELECT CASE WHEN CAB.TIPMOV = 'P' THEN TPO.BASENUMERACAO ELSE 'N' END
           ,CAB.STATUSNOTA
        INTO P_BASENUMERACAO, P_STATUSNOTA
      FROM TGFTOP TPO
         , TGFCAB CAB
      WHERE CAB.NUNOTA = :NEW.NUNOTA
        AND TPO.CODTIPOPER = CAB.CODTIPOPER
        AND TPO.DHALTER = CAB.DHTIPOPER;
      EXCEPTION WHEN NO_DATA_FOUND THEN
        RETURN;
      END;  
  END IF;
  
  IF (NVL(P_BASENUMERACAO,'N') <> 'A') OR (NVL(P_STATUSNOTA,'N') <> 'L') THEN  --Se não for DAV não estiver confirmada, retorna
    RETURN;
  END IF;
  
  IF INSERTING THEN
    SELECT SEQ_TGFDAV_SEQUENCIA.NEXTVAL 
     INTO P_NEWSEQUENCIA 
    FROM DUAL;
     
    INSERT INTO TGFDAV(NUNOTA, SEQUENCIA, CODLOGDAV, DTALTER, CODPROD, QTDNEG, CODVOL, VLRCUS,
                       VLRUNIT, VLRDESC, VLRTOT, CODTRIB, ALIQICMS, CANCELADO, TIPALTER)
                VALUES(:NEW.NUNOTA, :NEW.SEQUENCIA, P_NEWSEQUENCIA, SYSDATE, :NEW.CODPROD, :NEW.QTDNEG, :NEW.CODVOL, :NEW.VLRCUS, 
                       :NEW.VLRUNIT, :NEW.VLRDESC, :NEW.VLRTOT, :NEW.CODTRIB, :NEW.ALIQICMS, :NEW.CANCELADO, 'I');
  ELSIF UPDATING THEN
    IF :OLD.STATUSNOTA = :NEW.STATUSNOTA AND :OLD.QTDENTREGUE = :NEW.QTDENTREGUE THEN
      SELECT SEQ_TGFDAV_SEQUENCIA.NEXTVAL 
        INTO P_OLDSEQUENCIA 
      FROM DUAL;
          
      INSERT INTO TGFDAV(NUNOTA, SEQUENCIA, CODLOGDAV, DTALTER, CODPROD, QTDNEG, CODVOL, VLRCUS,
                         VLRUNIT, VLRDESC, VLRTOT, CODTRIB, ALIQICMS, CANCELADO, TIPALTER)
                  VALUES(:OLD.NUNOTA, :OLD.SEQUENCIA, P_OLDSEQUENCIA, SYSDATE, :OLD.CODPROD, :OLD.QTDNEG, :OLD.CODVOL, :OLD.VLRCUS, 
                         :OLD.VLRUNIT, :OLD.VLRDESC, :OLD.VLRTOT, :OLD.CODTRIB, :OLD.ALIQICMS, :OLD.CANCELADO, 'A');
    END IF;
  ELSE
    SELECT SEQ_TGFDAV_SEQUENCIA.NEXTVAL 
      INTO P_OLDSEQUENCIA 
    FROM DUAL;    
    INSERT INTO TGFDAV(NUNOTA, SEQUENCIA, CODLOGDAV, DTALTER, CODPROD, QTDNEG, CODVOL, VLRCUS,
                       VLRUNIT, VLRDESC, VLRTOT, CODTRIB, ALIQICMS, CANCELADO, TIPALTER)
                VALUES(:OLD.NUNOTA, :OLD.SEQUENCIA, P_OLDSEQUENCIA, SYSDATE, :OLD.CODPROD, :OLD.QTDNEG, :OLD.CODVOL, :OLD.VLRCUS, 
                       :OLD.VLRUNIT, :OLD.VLRDESC, :OLD.VLRTOT, :OLD.CODTRIB, :OLD.ALIQICMS, :OLD.CANCELADO, 'E');
  END IF;
END;

