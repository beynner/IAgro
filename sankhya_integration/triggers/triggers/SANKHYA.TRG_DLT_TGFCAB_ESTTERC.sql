-- SANKHYA.TRG_DLT_TGFCAB_ESTTERC
CREATE OR REPLACE TRIGGER SANKHYA.TRG_DLT_TGFCAB_ESTTERC
"SANKHYA".TRG_DLT_TGFCAB_ESTTERC 
BEFORE UPDATE OF CODPARC, CODTIPOPER, DHTIPOPER, CODPARCDEST ON TGFCAB  
FOR EACH ROW

DECLARE 
  P_ATUALESTTERC    CHAR(1);  
  P_ATUALESTTERC_D  CHAR(1);
  P_CODTIPOPERDENEG NUMBER(5);
  P_TOPNFEDENEG     NUMBER(10);
  ERRMSG            VARCHAR2(255);
  ERROR             EXCEPTION;
  P_VALIDAR BOOLEAN;
  P_CODCFOENTRADA      NUMBER(10);
  P_CODCFOENTRADA_FORA NUMBER(10);

BEGIN
  IF STP_GET_ATUALIZANDO THEN
    RETURN;
  END IF;
  /* 
  Sincronização de dados
  */
  P_VALIDAR := FPODEVALIDAR('TGFCAB');
 
  BEGIN
  SELECT NVL(ATUALESTTERC, 'N'), NVL(CODCFO_ENTRADA,0), NVL(CODCFO_ENTRADA_FORA,0) 
    INTO P_ATUALESTTERC, P_CODCFOENTRADA, P_CODCFOENTRADA_FORA 
    FROM TGFTOP 
   WHERE CODTIPOPER =:NEW.CODTIPOPER
     AND DHALTER = :NEW.DHTIPOPER;
  EXCEPTION WHEN NO_DATA_FOUND THEN 
    P_ATUALESTTERC := 'N';
    P_CODCFOENTRADA := 0;
    P_CODCFOENTRADA_FORA := 0;
  END;
  
  IF (P_CODCFOENTRADA IN (1122,2122) OR P_CODCFOENTRADA_FORA IN (1122,2122)) AND 
      P_ATUALESTTERC <> 'N' AND
      (NVL(:NEW.CODPARCDEST,0) <> NVL(:OLD.CODPARCDEST,0)) THEN
    ERRMSG := 'Não é possivel alterar o código do Paceiro de destino quando o lançamento atualiza estoque de terceiros.';
    RAISE ERROR;
  END IF;
  
  IF(:NEW.CODPARC <> :OLD.CODPARC) AND P_ATUALESTTERC <> 'N' THEN 
    ERRMSG := 'Não é possivel alterar o codigo do Paceiro quando o lancamento atualiza estoque de terceiros.';
    RAISE ERROR;
  END IF;
  
  BEGIN
  SELECT NVL(ATUALESTTERC, 'N'), NVL(CODTIPOPERDENEG,-1) 
    INTO P_ATUALESTTERC_D, P_CODTIPOPERDENEG  
    FROM TGFTOP 
   WHERE CODTIPOPER =:OLD.CODTIPOPER
     AND DHALTER = :OLD.DHTIPOPER;
  EXCEPTION WHEN NO_DATA_FOUND THEN 
    P_ATUALESTTERC_D := 'N';
    P_CODTIPOPERDENEG  := -1;
  END;
    
  BEGIN
    SELECT NVL(INTEIRO,-1)
    INTO P_TOPNFEDENEG
    FROM TSIPAR
    WHERE CHAVE = 'TOPNFEDENEG';
  EXCEPTION WHEN NO_DATA_FOUND THEN    
    P_TOPNFEDENEG := -1;
  END;
  
  IF ((P_ATUALESTTERC<>P_ATUALESTTERC_D) AND (:NEW.CODTIPOPER NOT IN(P_CODTIPOPERDENEG,P_TOPNFEDENEG))) THEN 
      ERRMSG := 'Não é possivel alteraro Tipo de atualização de estoque de terceiros.';
    RAISE ERROR;
  END IF;
      
  RETURN;  
EXCEPTION
  WHEN ERROR THEN
    /* 
    Sincronizac?o de dados n?o faz validac?es
    */
    IF (P_VALIDAR) THEN 
      RAISE_APPLICATION_ERROR(-20101, ERRMSG);
    END IF; 
END;

