-- SANKHYA.TRG_INC_TGFITE_AFTER
CREATE OR REPLACE TRIGGER SANKHYA.TRG_INC_TGFITE_AFTER
"SANKHYA".TRG_INC_TGFITE_AFTER
AFTER INSERT ON TGFITE

DECLARE
   CURSOR TRANSF IS SELECT NUNOTA, SEQUENCIA, CODEMPNEGOC, CFO, ATUALESTTERC, TERCEIROS, TIPMOV, PENDENTE
                    FROM TGFITE_INC;
   P_NUNOTA                  INT;
   P_SEQUENCIA               NUMBER(5);
   P_CODEMPNEGOC             NUMBER(5);
   P_CODCFO                  INT;
	P_ATUALESTTERC				  CHAR(1);
   P_TIPMOV                  CHAR(1);
   P_PENDENTE                CHAR(1);
   P_PENDENTE_CAB			 CHAR(1);
   P_PENDENTE_TOP			 CHAR(1);
   P_COUNT                   INT := 0;
	P_TERCEIROS 	   TGFITE_UPD.TERCEIROS%TYPE;
BEGIN
  IF STP_GET_ATUALIZANDO THEN
    RETURN;
  END IF;

   OPEN TRANSF;
   DELETE FROM TGFITE_INC;
   LOOP
     FETCH TRANSF INTO P_NUNOTA, P_SEQUENCIA, P_CODEMPNEGOC, P_CODCFO, P_ATUALESTTERC, P_TERCEIROS, P_TIPMOV, P_PENDENTE;
     EXIT WHEN TRANSF%NOTFOUND;
      
	  IF P_TIPMOV = 'T' THEN
		  INSERT INTO TGFITE
		   (NUNOTA, SEQUENCIA, CODPROD, CODEMP, CODLOCALORIG, CONTROLE, CODCFO, QTDNEG,
			QTDCONFERIDA, VLRUNIT, VLRCUS, BASEIPI, VLRIPI, BASEICMS, VLRICMS, VLRDESC,
			BASESUBSTIT, VLRSUBST, CODVOL, CODTRIB, ATUALESTOQUE, OBSERVACAO, NUTAB,
			RESERVA, STATUSNOTA, PENDENTE, ALIQICMS,VLRTOT, FATURAR, USOPROD, ALIQIPI, CODOBSPADRAO, ATUALESTTERC, TERCEIROS, 
			PERCPUREZA, PERCGERMIN, STATUSLOTE, BASEISS, VLRISS, CODTPA, SERIEINICIAL, SERIEFINAL, QTDWMS)
		  SELECT I.NUNOTA, (P_SEQUENCIA * (-1)), I.CODPROD, P_CODEMPNEGOC, I.CODLOCALORIG,
				  I.CONTROLE, P_CODCFO, I.QTDNEG, I.QTDCONFERIDA, I.VLRUNIT, I.VLRCUS, I.BASEIPI,
				  I.VLRIPI, I.BASEICMS, I.VLRICMS, I.VLRDESC, I.BASESUBSTIT, I.VLRSUBST,
				  I.CODVOL, I.CODTRIB, (ATUALESTOQUE * -1), I.OBSERVACAO, I.NUTAB, I.RESERVA,
				  I.STATUSNOTA, 'N', ALIQICMS, VLRTOT, 'F', USOPROD, ALIQIPI, CODOBSPADRAO, P_ATUALESTTERC, P_TERCEIROS,
				  I.PERCPUREZA, I.PERCGERMIN, I.STATUSLOTE, I.BASEISS, I.VLRISS, I.CODTPA, I.SERIEINICIAL, I.SERIEFINAL, I.QTDWMS
		   FROM TGFITE I
		   WHERE I.NUNOTA = P_NUNOTA
			 AND I.SEQUENCIA = P_SEQUENCIA;
	  END IF;
		 
	  /* atualizacao do campo pendente do item e do cabecalho */
       IF P_TIPMOV IN ('O','P','C', '8','N','1','2','J','V','Q') THEN
		  SELECT C.PENDENTE, T.PENDENTE INTO
				 P_PENDENTE_CAB, P_PENDENTE_TOP
		  FROM TGFCAB C, TGFTOP T
		  WHERE C.NUNOTA = P_NUNOTA
		  AND C.CODTIPOPER = T.CODTIPOPER
		  AND C.DHTIPOPER = T.DHALTER;
		  
	     IF P_PENDENTE_TOP = 'S' THEN
			 IF (P_PENDENTE_CAB = 'N' AND P_PENDENTE = 'S') THEN
				UPDATE TGFCAB SET PENDENTE = 'S'
				WHERE NUNOTA = P_NUNOTA;
			 END IF;
			 IF (P_PENDENTE_CAB = 'S' AND P_PENDENTE = 'N' AND P_TIPMOV <> '8') THEN
				SELECT COUNT(1) INTO P_COUNT
				FROM TGFITE
				WHERE NUNOTA = P_NUNOTA
				AND PENDENTE = 'S';
				IF (P_COUNT = 0 ) THEN
				   UPDATE TGFCAB SET PENDENTE = 'N'
				   WHERE NUNOTA = P_NUNOTA
				   AND PENDENTE = 'S';
				END IF;
			 END IF;
		 END IF;
       END IF;
		 
    END LOOP;
    CLOSE TRANSF;
END;

/
