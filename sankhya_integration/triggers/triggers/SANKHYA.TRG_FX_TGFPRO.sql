-- SANKHYA.TRG_FX_TGFPRO
CREATE OR REPLACE TRIGGER SANKHYA.TRG_FX_TGFPRO
TRG_FX_TGFPRO
   BEFORE INSERT OR UPDATE OR DELETE
   ON TGFPRO
   FOR EACH ROW

DECLARE
   P_CONTADOR    NUMBER (19);
   P_VALIDAR     BOOLEAN;
   P_CODBARRA    TGFPRO.REFERENCIA%TYPE;
   P_CODVOL      VARCHAR2 (3);
   P_LENIMGNEW   INT;
   P_LENIMGOLD   INT;
   P_TEM_CHECKOUT BOOLEAN;
   P_CONFIGKIT   BOOLEAN;
   
BEGIN
   IF Stp_Get_Atualizando
   THEN
      RETURN;
   END IF;

   /*
   Sincronizacao de dados
   */
   P_VALIDAR := Fpodevalidar ('TGFPRO');

   IF (NOT P_VALIDAR)
   THEN
      RETURN;
   END IF;

   P_TEM_CHECKOUT := FCheckout_Utiliza;
  
  IF (NOT P_TEM_CHECKOUT) THEN
    RETURN;
  END IF;
  
  P_CONFIGKIT := FTEM_KIT_INDEPENDENTE;

   IF (DELETING)
   THEN
      INSERT INTO TFXADE (NOMETAB, STRPK)
          VALUES ('TPRODUTO', 'CODPRODUTO = ' || TO_CHAR (:OLD.CODPROD));

      INSERT INTO TFXADE (NOMETAB, STRPK)
          VALUES ('TEANPROD', 'CODPRODUTO = ' || TO_CHAR (:OLD.CODPROD));

      INSERT INTO TFXADE (NOMETAB, STRPK)
          VALUES ('TPRECO', 'CODPRODUTO = ' || TO_CHAR (:OLD.CODPROD));

      INSERT INTO TFXADE (NOMETAB, STRPK)
          VALUES ('TIMPOSTO', 'CODPRODUTO = ' || TO_CHAR (:OLD.CODPROD) );

      RETURN;
   END IF;

   IF (:NEW.REFERENCIA IS NULL AND :OLD.REFERENCIA IS NOT NULL)
   THEN
      DELETE TFXEAN WHERE (CODBARRA = :OLD.REFERENCIA OR NVL(CODBARRA,'') = '') AND CODPROD = :OLD.CODPROD;
   END IF;

   IF (   (NVL (:NEW.CODPROD, 0) <> NVL (:OLD.CODPROD, 0))
       OR (NVL (:NEW.DESCRPROD, '') <> NVL (:OLD.DESCRPROD, ''))
       OR (:NEW.DESCRPROD <> :OLD.DESCRPROD)
       OR (:NEW.COMPLDESC <> :OLD.COMPLDESC)
       OR (:NEW.CLASSUBTRIB <> :OLD.CLASSUBTRIB)
       OR (:NEW.DECVLR <> :OLD.DECVLR)
       OR (NVL (:NEW.DECQTD, 0) <> NVL (:OLD.DECQTD, 0))
       OR (:NEW.DESCMAX <> :OLD.DESCMAX)
       OR (:NEW.PESOBRUTO <> :OLD.PESOBRUTO)
       OR (:NEW.PESOLIQ <> :OLD.PESOLIQ)
       OR (:NEW.QTDEMB <> :OLD.QTDEMB)
       OR (:NEW.PROMOCAO <> :OLD.PROMOCAO)
       OR (:NEW.USOPROD <> :OLD.USOPROD)
       OR (:NEW.ORIGPROD <> :OLD.ORIGPROD)
       OR (:NEW.TIPSUBST <> :OLD.TIPSUBST)
       OR (:NEW.CODTAB <> :OLD.CODTAB)
       OR (:NEW.ATIVO <> :OLD.ATIVO)
       OR (:NEW.CODGRUPOPROD <> :OLD.CODGRUPOPROD)
       OR (:NEW.GRUPOICMS <> :OLD.GRUPOICMS)
       OR (:NEW.CNAE <> :OLD.CNAE)
       OR (:NEW.GRUPOPIS <> :OLD.GRUPOPIS)
       OR (:NEW.GRUPOCOFINS <> :OLD.GRUPOCOFINS)
       OR (:NEW.GRUPOCSSL <> :OLD.GRUPOCSSL)
       OR (:NEW.BALANCA <> :OLD.BALANCA)
       OR (:NEW.NCM <> :OLD.NCM)
       OR (:NEW.CODBARBALANCA <> :OLD.CODBARBALANCA)
       OR (:NEW.ALIQNAC <> :OLD.ALIQNAC)
       OR (:NEW.ALIQIMP <> :OLD.ALIQIMP)
       OR (:NEW.DESCRPRODNFE <> :OLD.DESCRPRODNFE)
       OR (:NEW.GRUPOICMS2 <> :OLD.GRUPOICMS2)
       OR (:NEW.ALIQGERAL <> :OLD.ALIQGERAL)
       OR (:NEW.TIPOSN <> :OLD.TIPOSN)
       OR (:NEW.INTEGRAFOX <> :OLD.INTEGRAFOX)
       OR (:NEW.PERCCMTFED <> :OLD.PERCCMTFED)
       OR (:NEW.PERCCMTEST <> :OLD.PERCCMTEST)
       OR (:NEW.PERCCMTMUN <> :OLD.PERCCMTMUN)
       OR (:NEW.PERCCMTIMP <> :OLD.PERCCMTIMP)
       OR (:NEW.PERCCMTNAC <> :OLD.PERCCMTNAC)
       OR (:NEW.CODANVISA <> :OLD.CODANVISA)
       OR (:NEW.REFERENCIA <> :OLD.REFERENCIA)
       OR (:NEW.GRUPODESCPROD <> :OLD.GRUPODESCPROD)
       /* DESCE APENAS OS CONTROLES SERIE, LISTA E LOTE */
       OR ((NVL( :NEW.TIPCONTEST, 'N') <> NVL( :OLD.TIPCONTEST, 'N')) AND (NVL( :NEW.TIPCONTEST, 'N') IN ('S', 'E', 'L') )) 
       OR ((NVL( :NEW.LISCONTEST, ' ') <> NVL( :OLD.LISCONTEST, ' ')) AND (NVL( :NEW.TIPCONTEST, 'N') IN ('S', 'E', 'L') ))
       OR ((NVL( :NEW.TITCONTEST, ' ') <> NVL( :OLD.TITCONTEST, ' ')) AND (NVL( :NEW.TIPCONTEST, 'N') IN ('S', 'E', 'L') ))
	   OR (:NEW.USACODBARRASQTD <> :OLD.USACODBARRASQTD)
       OR (:NEW.MARCA <> :OLD.MARCA)
       OR (:NEW.CNPJFABRICANTE <> :OLD.CNPJFABRICANTE)
       OR (:NEW.INDESCALA <> :OLD.INDESCALA)
       OR (:NEW.CODBENEFNAUF <> :OLD.CODBENEFNAUF)
     /* CAMPOS RELACIONADOS AO KIT */
     OR ((P_CONFIGKIT) AND ((NVL( :NEW.TIPOKIT, ' ') <> NVL( :OLD.TIPOKIT, ' ')) 
                                      OR (NVL( :NEW.CODPRODSUBKIT, 0) <> NVL( :OLD.CODPRODSUBKIT, 0)) 
                            OR (NVL( :NEW.CODCONFKIT, 0) <> NVL( :OLD.CODCONFKIT, 0)))))
   THEN
      SELECT SEQ_GLOBALFOX.NEXTVAL INTO P_CONTADOR FROM DUAL;

      :NEW.NUVERSAO := P_CONTADOR;

       IF (:OLD.INTEGRAFOX <> -1)
         AND ( (:NEW.ATIVO <> :OLD.ATIVO)
              OR (:NEW.CODGRUPOPROD <> :OLD.CODGRUPOPROD)
              /* SE O TIPO DE CONTROLE ERA SERIE, LISTA OU LOTE E DEIXOU DE SER UM DELES, DESCO APENAS PARA ATUALIZAR O CONTROLE NO CHECKOUT*/
              OR ((NVL( :NEW.TIPCONTEST, 'N') <> NVL( :OLD.TIPCONTEST, 'N')) AND (  NVL( :NEW.TIPCONTEST, 'N') NOT IN ('S', 'E', 'L') AND NVL( :OLD.TIPCONTEST, 'N') IN ('S', 'E', 'L'))))
      THEN
         :NEW.INTEGRAFOX := -1;
      END IF;
   END IF;

   IF (:NEW.REFERENCIA IS NOT NULL) AND((NVL (:NEW.REFERENCIA, ' ') <> NVL (:OLD.REFERENCIA, ' '))
        OR (:NEW.CODVOL <> :OLD.CODVOL)) THEN
      P_CODBARRA := :NEW.REFERENCIA;
      P_CODVOL := :NEW.CODVOL;

      IF (:OLD.REFERENCIA IS NOT NULL)
      THEN
         P_CODBARRA := :OLD.REFERENCIA;
      END IF;

      IF (:OLD.CODVOL IS NOT NULL)
      THEN
         P_CODVOL := :OLD.CODVOL;
      END IF;

      SELECT COUNT (1)
        INTO P_CONTADOR
        FROM TFXEAN E
       WHERE E.CODBARRA = P_CODBARRA AND E.VOLUMEPRINCIPAL = 'S';

      IF (NVL (P_CONTADOR, 0) > 0)
      THEN
		  UPDATE TFXEAN
			SET CODBARRA = :NEW.REFERENCIA,
                            CODVOL = :NEW.CODVOL,
			    CODPROD = :NEW.CODPROD,
                            UNIDTRIB = 'S',
		            ORIGEMEAN = 1,
			    CONTROLE = NULL,
			    CODLOCAL = NULL 
			WHERE CODBARRA = P_CODBARRA AND VOLUMEPRINCIPAL = 'S';
      ELSE 
        IF P_CODBARRA IS NOT NULL THEN      
			INSERT INTO TFXEAN (	CODBARRA,
								CODVOL,
								CODPROD,
								VOLUMEPRINCIPAL,
								DIVIDEMULTIPLICA,
								QUANTIDADE,                            
								UNIDTRIB,
								ORIGEMEAN,
								CONTROLE,
								CODLOCAL)
            VALUES (	:NEW.REFERENCIA,
						:NEW.CODVOL,
						:NEW.CODPROD,
						'S',
						'M',
						1,
						'S',
						1,
						NULL,
						NULL);
        END IF;
      END IF;
   END IF;

   /*
   * CONTROLAR A RECALCULO DE IMPOSTO
   */
   IF ( (:NEW.PRODUTONFE <> :OLD.PRODUTONFE)
       OR (:NEW.TIPGTINNFE <> :OLD.TIPGTINNFE)
       OR (:NEW.PRODUTONFE <> :OLD.PRODUTONFE) 
       OR (:NEW.CLASSUBTRIB <> :OLD.CLASSUBTRIB)
       OR (:NEW.USOPROD <> :OLD.USOPROD)
       OR (:NEW.ORIGPROD <> :OLD.ORIGPROD)
       OR (:NEW.TIPSUBST <> :OLD.TIPSUBST)
       OR (:NEW.CODGRUPOPROD <> :OLD.CODGRUPOPROD)
       OR (:NEW.GRUPOICMS <> :OLD.GRUPOICMS)
       OR (:NEW.CNAE <> :OLD.CNAE)
       OR (:NEW.GRUPOPIS <> :OLD.GRUPOPIS)
       OR (:NEW.GRUPOCOFINS <> :OLD.GRUPOCOFINS)
       OR (:NEW.GRUPOCSSL <> :OLD.GRUPOCSSL)
       OR (:NEW.ALIQNAC <> :OLD.ALIQNAC)
       OR (:NEW.ALIQIMP <> :OLD.ALIQIMP)
       OR (:NEW.GRUPOICMS2 <> :OLD.GRUPOICMS2)
       OR (:NEW.ALIQGERAL <> :OLD.ALIQGERAL)
       OR (:NEW.PERCCMTFED <> :OLD.PERCCMTFED)
       OR (:NEW.PERCCMTEST <> :OLD.PERCCMTEST)
       OR (:NEW.PERCCMTMUN <> :OLD.PERCCMTMUN)
       OR (:NEW.PERCCMTIMP <> :OLD.PERCCMTIMP)
       OR (:NEW.PERCCMTNAC <> :OLD.PERCCMTNAC)
       OR (:NEW.CODBENEFNAUF <> :OLD.CODBENEFNAUF)) 
	  THEN
		SELECT COUNT (1)
        INTO P_CONTADOR
        FROM TFXIPV E
		WHERE E.CODPROD = :NEW.CODPROD AND CODIMP = 0;

		IF (NVL (P_CONTADOR, 0) > 0)
		THEN
			UPDATE TFXIPV
            SET RECALCULAR = 'S'
			WHERE CODPROD = :NEW.CODPROD AND CODIMP = 0 AND CODEMP IN (SELECT CHK.CODEMPRESA FROM TFXPDV CHK WHERE CHK.CODEMPRESA > 0 AND CHK.CONFIGURADO = 'S');
		END IF;
   END IF;

   /*
    * CONTROLAR MUDANCA DA IMAGEM, O SERVICO IRA MONITORAR ESSE CAMPO PARA LEVAR APENAS A IMAGEM
    * */
   P_LENIMGNEW := NVL (DBMS_LOB.getlength (:NEW.IMAGEM), 0);
   P_LENIMGOLD := NVL (DBMS_LOB.getlength (:OLD.IMAGEM), 0);

   IF ( (P_LENIMGNEW <> P_LENIMGOLD) OR (:NEW.ENDIMAGEM <> :OLD.ENDIMAGEM))
   THEN
      SELECT SEQ_GLOBALFOX.NEXTVAL INTO P_CONTADOR FROM DUAL;

      :NEW.NUVERSAOIMG := P_CONTADOR;
   END IF;
END;

