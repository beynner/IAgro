-- SANKHYA.TRG_INC_UPD_DLT_TGFFIN_SSPMB
CREATE OR REPLACE TRIGGER SANKHYA.TRG_INC_UPD_DLT_TGFFIN_SSPMB
"SANKHYA".TRG_INC_UPD_DLT_TGFFIN_SSPMB 
BEFORE INSERT OR UPDATE OR DELETE
ON TGFFIN
FOR EACH ROW

DECLARE
  P_COUNT       INTEGER;
  P_ULTCOD      INTEGER;
  P_CGC_CPF     VARCHAR2(14);
  P_VALIDAR     BOOLEAN;
  P_VLRDESC     FLOAT;
  P_CODUSU      TSIUSU.CODUSU%TYPE;
  ERRMSG        VARCHAR2(4000);
  ERROR         EXCEPTION;
  
  PRAGMA AUTONOMOUS_TRANSACTION;

BEGIN
  IF STP_GET_ATUALIZANDO THEN
    RETURN;
  END IF;

  P_VALIDAR := Fpodevalidar('TGFFIN'); -- sincronização de dados
  
  --Se não tem o opcional, não faz nada. 
  SELECT COUNT(1) INTO P_COUNT
  FROM TSIPER
  WHERE PERMISSAO = 466860
  AND CODGRUPO = 0
  AND CODUSU = 0; 
  IF P_COUNT = 0 THEN
    RETURN;
  END IF;
  
  IF NVL(:NEW.TIPOABATSSPMB,'0') = '1' THEN
    RETURN;
  END IF;

  IF INSERTING AND :NEW.HISTORICO LIKE '%Switch Selling%' THEN
    :NEW.HISTORICO := NULL;
    :NEW.VLRDESCSSPMB := 0;
    IF NVL(:NEW.TIPOABATSSPMB,'.') = '0' THEN
      :NEW.TIPOABATSSPMB := NULL; 
    END IF;
  END IF;
  
  --So faz se for financeiro de nota de venda.
  SELECT COUNT(1) INTO P_COUNT
  FROM TGFCAB 
  WHERE NUNOTA = NVL(:NEW.NUNOTA, :OLD.NUNOTA)
  AND TIPMOV = 'V';
  IF P_COUNT = 0 THEN
    RETURN; 
  END IF;
  
  IF NOT ((DELETING  AND :OLD.ORIGEM = 'E' AND :OLD.PROVISAO = 'N' AND :OLD.RECDESP = 1) OR
          (INSERTING AND :NEW.ORIGEM = 'E' AND :NEW.PROVISAO = 'N' AND :NEW.RECDESP = 1 AND :NEW.DHBAIXA IS NULL AND :NEW.NUMNOTA > 0) OR 
          (UPDATING  AND :NEW.ORIGEM = 'E' AND :NEW.PROVISAO = 'N' AND :NEW.RECDESP = 1 AND :NEW.DHBAIXA IS NULL AND :NEW.NUMNOTA > 0 AND :OLD.NUMNOTA = 0))
  THEN
    RETURN;
  END IF;
  
  SELECT MAX(CGC_CPF) INTO P_CGC_CPF
  FROM TGFPAR 
  WHERE CODPARC = NVL(:NEW.CODPARC, :OLD.CODPARC);
  
  IF (INSERTING OR UPDATING) THEN
    P_CODUSU := :NEW.CODUSU;
    P_VLRDESC := 0;
    
    FOR RES IN (
      SELECT 'TGFPMBSS' TABELA
      , CAMPANHA
      , SEMANA
      , MES
      , DIAVENDA
      , TRUNC(0) AS NUMOV
      , CNPJ
      , VALORFINALCRED
      FROM TGFPMBSS 
      WHERE CNPJ = P_CGC_CPF
      AND NUMNOTA IS NULL
      
      UNION ALL
      
      SELECT 'TGFMOVSS'
      , NULL
      , NULL
      , NULL
      , NULL
      , NUMOV
      , CNPJ
      , VLRMOVIMENTO
      FROM TGFMOVSS 
      WHERE CNPJ = P_CGC_CPF
      AND TIPOLANC = 'M'
      AND NUMNOTA IS NULL
    )
    LOOP
      IF (P_VLRDESC + RES.VALORFINALCRED) < (:NEW.VLRDESDOB - :NEW.VLRDESC - :NEW.VLRDESCSSPMB) THEN
        P_VLRDESC := P_VLRDESC + RES.VALORFINALCRED;
        
        IF RES.TABELA = 'TGFPMBSS' THEN 
          UPDATE TGFPMBSS 
          SET NUMNOTA = :NEW.NUMNOTA
          , DATAPAGAMENTO = :NEW.DTNEG 
          , DHALTER = SYSDATE
          , CODUSU = P_CODUSU
          WHERE CNPJ = RES.CNPJ
          AND CAMPANHA = RES.CAMPANHA
          AND SEMANA = RES.SEMANA
          AND MES = RES.MES
          AND DIAVENDA = RES.DIAVENDA;
        END IF;
        
        IF RES.TABELA = 'TGFMOVSS' THEN 
          UPDATE TGFMOVSS
          SET NUMNOTA = :NEW.NUMNOTA
          , DATAPAGAMENTO = :NEW.DTNEG
          , NUFIN = :NEW.NUFIN
          , DHALTER = SYSDATE
          , CODUSU = P_CODUSU
          WHERE NUMOV = RES.NUMOV;
        END IF;
        
      END IF; 
    END LOOP;
    
    IF P_VLRDESC > 0 THEN
      SELECT COUNT(1) INTO P_ULTCOD
      FROM TGFNUM
      WHERE ARQUIVO = 'TGFMOVSS';
      
      IF P_ULTCOD = 0 THEN
        P_ULTCOD := 1;
        INSERT INTO TGFNUM(ARQUIVO
        , CODEMP
        , ULTCOD
        , ULTNOTATALAO)VALUES('TGFMOVSS'
        , 1
        , 1
        , 999999999);
      ELSE 
        SELECT NVL(MAX(ULTCOD), 0) + 1 INTO P_ULTCOD
        FROM TGFNUM
        WHERE ARQUIVO = 'TGFMOVSS';
           
        UPDATE TGFNUM SET ULTCOD = P_ULTCOD
        WHERE ARQUIVO = 'TGFMOVSS';
      END IF;      
      
      INSERT INTO TGFMOVSS 
        ( NUMOV
        , CNPJ
        , VLRMOVIMENTO
        , SINAL
        , NUFIN
        , DTLANCTO
        , OBSERVACOES
        , DHALTER
        , CODUSU)
      VALUES 
        ( P_ULTCOD
        , P_CGC_CPF
        , P_VLRDESC
        , -1
        , :NEW.NUFIN
        , :NEW.DTNEG
        , 'UTILIZACAO DE CREDITO NA NOTA NRO '||:NEW.NUMNOTA
        , SYSDATE
        , P_CODUSU);
  /*  
      UPDATE TGFCAB
      SET OBSERVACAO = CASE WHEN OBSERVACAO IS NULL THEN 'Switch Selling R$ ' ELSE OBSERVACAO ||' Switch Selling R$ ' END ||TRIM(TO_CHAR(P_VLRDESC, '999G999G999D99'))
      , CODUSU = :NEW.CODUSU
      , DTALTER = SYSDATE
      WHERE NUNOTA = :NEW.NUNOTA
      AND (OBSERVACAO IS NULL OR OBSERVACAO NOT LIKE '%Switch Selling%');
*/      
      COMMIT;
      
      :NEW.TIPOABATSSPMB := '0';
      :NEW.VLRDESCSSPMB := P_VLRDESC;
      :NEW.VLRDESC := :NEW.VLRDESC - NVL(:OLD.VLRDESCSSPMB, 0) + P_VLRDESC;
      IF (:NEW.HISTORICO IS NULL OR NOT :NEW.HISTORICO LIKE '%Switch Selling%') THEN
        :NEW.HISTORICO := CASE WHEN :NEW.HISTORICO IS NULL THEN 'Switch Selling R$ ' ELSE :NEW.HISTORICO ||' Switch Selling R$ ' END ||TRIM(TO_CHAR(P_VLRDESC, '999G999G999D99'));
      END IF;
      
    END IF;
    
  END IF;

  IF DELETING THEN
    P_CODUSU := STP_GET_CODUSULOGADO;
    
    SELECT COUNT(1) INTO P_COUNT
    FROM  TGFMOVSS
    WHERE NUFIN = :OLD.NUFIN
    AND TIPOLANC = 'A';
    
    IF(P_COUNT <> 0) THEN
      DELETE FROM TGFMOVSS
      WHERE NUFIN = :OLD.NUFIN
      AND TIPOLANC = 'A';
      
      UPDATE TGFPMBSS 
      SET NUMNOTA = NULL
      , DATAPAGAMENTO = NULL 
      , DHALTER = SYSDATE
      , CODUSU = P_CODUSU
      WHERE CNPJ = P_CGC_CPF
      AND NUMNOTA = :OLD.NUMNOTA;
      
      UPDATE TGFMOVSS
      SET NUMNOTA = NULL
      , DATAPAGAMENTO = NULL
      , NUFIN = 0
      , DHALTER = SYSDATE
      , CODUSU = P_CODUSU
      WHERE NUFIN = :OLD.NUFIN
      AND TIPOLANC = 'M';
      
      COMMIT;
    END IF;
    
  END IF;  

  RETURN;

EXCEPTION
  WHEN OTHERS THEN
    /*
    Sincronização de dados não faz validações
    */
     IF (P_VALIDAR) THEN
       IF SQLCODE <> 1 THEN
         ERRMSG := ERRMSG || '  ' || SQLERRM;
       END IF;
       RAISE_APPLICATION_ERROR(-20101, ERRMSG);       
     END IF;
END;

